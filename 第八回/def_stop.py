# ============================================================
# Stop クラスの定義（バス停1件分のデータ）
# ============================================================
# このクラスは、京都のバス停1件分の情報を保持するためのクラス。
#
# ■ 保持する情報：
#   - id: バス停のID（文字列）例: 'ED01_1914'
#   - name: バス停の名前（文字列）例: '立命館大学前'
#   - lat: 緯度（float型）例: 35.03497163
#   - lng: 経度（float型）例: 135.72602961
#     ※ lng はソートのキー（比較に使う値）として使用される
#     ※ 全てのソートアルゴリズム（ヒープ, マージ, クイック）で
#       lng を基準に昇順ソート（西→東の順）を行う
#   - routes: 通過する路線のリスト（文字列のリスト）
#     例: ['12号系統', '15号系統', '50号系統']
#
# ■ ソートとの関係：
#   - ヒープソート（8-1.py）: ヒープ内で stop.lng を比較して最小ヒープを構築、
#     delete_min で最小経度のStopを取り出し、enqueue/dequeueで順序を保持
#   - マージソート（8-2.py）: _merge() 内で left[i].lng と right[j].lng を比較
#   - クイックソート（8-3.py）: _partition() 内で self.list[j].lng と pivot を比較
# ============================================================


# Stopクラスの定義
class Stop:
    def __init__(self, id, name, lat, lng, routes):  # 初期化メソッド
        self.id = id  # バス停ID（文字列）
        self.name = name  # バス停名（文字列）
        self.lat = lat  # 緯度（float）— 北緯の値
        self.lng = lng  # 経度（float）— 東経の値 ← ソートのキーとして使われる
        self.routes = routes  # 通過路線のリスト（文字列のリスト）

    def countRoutes(self):
        # このバス停を通る路線の数を返すメソッド
        return len(self.routes)


