# ============================================================
# DB クラスの定義（データベースの基底クラス）
# ============================================================
# このファイルでは、バス停データを管理するための基底クラス DB を定義する。
#
# ■ DB クラスの役割：
#   - バス停データ（Stop オブジェクト）をリストに格納し、
#     追加・表示・件数取得・ソート済み判定といった共通機能を提供する
#   - sort() メソッドは pass（何もしない）になっており、
#     子クラス（DBwHeapSort, DBwMergeSort, DBwQuickSort など）で
#     具体的なソートアルゴリズムをオーバーライドして使う設計になっている
#
# ■ 継承を前提とした設計：
#   - DB クラス自体は「親クラス（基底クラス）」として機能する
#   - 子クラスは DB の全メソッド（add, display, count, is_sorted）を引き継ぎ、
#     sort() だけを再定義（オーバーライド）する
#   - これにより、ソートアルゴリズムだけを差し替えて
#     性能比較ができる構造になっている
#
# ■ 第8回で使う子クラス：
#   - DBwHeapSort（8-1.py）: ヒープソート — ヒープ+キュー（enqueue/dequeue）を使用
#   - DBwMergeSort（8-2.py）: マージソート — 再帰的に分割→マージ
#   - DBwQuickSort（8-3.py）: クイックソート — パーティション+再帰
#
# ■ クラスの継承関係図：
#
#        DB（親クラス / 基底クラス）
#        ├── add()       ← データ追加（共通）
#        ├── display()   ← データ表示（共通）
#        ├── count()     ← 件数取得（共通）
#        ├── is_sorted() ← ソート検証（共通）
#        └── sort()      ← pass（何もしない）
#              ↓ オーバーライド
#        ┌─────────────────────────────────────────┐
#        │  DBwHeapSort   │ DBwMergeSort │ DBwQuickSort │
#        │  sort() を     │ sort() を    │ sort() を    │
#        │  ヒープソートで│ マージソートで│ クイックソートで│
#        │  再定義        │ 再定義       │ 再定義       │
#        └─────────────────────────────────────────┘
#
# ■ is_sorted() によるソート検証：
#   - 隣り合う要素の経度(lng)を順番に比較し、
#     前の要素が後の要素より大きい箇所が1つでもあれば False を返す
#   - 全ての隣り合う要素が正しい昇順関係にあれば True を返す
# ============================================================


class DB:  # DBクラスの定義
    def __init__(self):
        # インスタンス変数 self.list を空リストで初期化
        # ここに Stop オブジェクトが順番に格納されていく
        self.list = []  # データを格納するリスト

    def add(self, data):
        # 引数 data（Stopオブジェクト）をリストの末尾に追加する
        # append() はリストの末尾に要素を1つ追加するメソッド
        # ※ キューの enqueue() と同じ操作だが、ここでは単純にデータを追加するだけ
        self.list.append(data)  # データをリストの末尾に挿入

    def display(self):
        # リスト内の全データを番号付きで表示するメソッド
        # format() を使って、通し番号・名前・ID・緯度・経度を埋め込む
        message = '{}： {}(ID:{})のバス停の緯度経度は({},{})です。'
        j = 1  # 表示用の通し番号（1から始まる）
        for i in self.list:  # self.list の各 Stop オブジェクトに対してループ
            # i.name → バス停名, i.id → ID, i.lat → 緯度, i.lng → 経度
            print(message.format(j, i.name, i.id, i.lat, i.lng))
            j += 1  # 通し番号をインクリメント

    def count(self):  # データの件数を返す
        # len() でリストの要素数を取得して返す
        return len(self.list)

    def sort(self):  # データを昇順にソートする（サブクラスでオーバーライド）
        # 親クラスでは何もしない（子クラスでオーバーライドする前提）
        # pass は「何もしない」を意味するPythonの文
        # 子クラス（DBwHeapSort等）で独自のソート処理に書き換えられる
        pass

    def is_sorted(self):
        # ソートが正しく完了しているか（昇順になっているか）を検証するメソッド
        # 隣り合う要素の経度(lng)を順番に比較していく
        #
        # 【動作例】
        #   self.list の経度が [135.3, 135.5, 135.7, 135.9] の場合:
        #     i=0: 135.3 > 135.5 → False → OK
        #     i=1: 135.5 > 135.7 → False → OK
        #     i=2: 135.7 > 135.9 → False → OK
        #     → 全て OK なので True を返す
        #
        #   self.list の経度が [135.3, 135.9, 135.5, 135.7] の場合:
        #     i=0: 135.3 > 135.9 → False → OK
        #     i=1: 135.9 > 135.5 → True → NG! → False を返す
        for i in range(self.count() - 1):  # 隣り合う要素を順番に比較
            # 前の要素の経度が次の要素の経度より大きい場合、昇順になっていない
            if self.list[i].lng > self.list[i + 1].lng:
                return False
        return True  # 全ての隣り合う要素が正しい昇順関係にあれば True
