# ============================================================
# Queue（キュー）と Stack（スタック）クラスの定義
# ============================================================
# このファイルでは、基本的なデータ構造である
# キュー（Queue）とスタック（Stack）をリストを使って実装する。
#
# ■ キュー（Queue）— FIFO: First In, First Out（先入れ先出し）
#   - 最初に入れたデータが最初に取り出される
#   - 例: レジの行列 — 先に並んだ人から順番に対応される
#   - 用途: タスクの順番管理、幅優先探索（BFS）、プリンタのジョブ管理など
#   - 操作:
#     - enqueue(data): データをキューの末尾に追加する（列の最後尾に並ぶイメージ）
#     - dequeue(): データをキューの先頭から取り出す（列の先頭の人が対応されるイメージ）
#
#   【enqueue と dequeue の動作イメージ（レジの行列）】
#
#     enqueue("田中") → [田中]
#     enqueue("鈴木") → [田中, 鈴木]
#     enqueue("佐藤") → [田中, 鈴木, 佐藤]
#     dequeue() → 田中 を返す   残り: [鈴木, 佐藤]   ← 最初に並んだ田中が先に出る
#     dequeue() → 鈴木 を返す   残り: [佐藤]
#     enqueue("山田") → [佐藤, 山田]
#     dequeue() → 佐藤 を返す   残り: [山田]
#     dequeue() → 山田 を返す   残り: []
#     dequeue() → None を返す   ← キューが空なので None
#
# ■ スタック（Stack）— LIFO: Last In, First Out（後入れ先出し）
#   - 最後に入れたデータが最初に取り出される
#   - 例: 積み上げた本 — 一番上（最後に置いた本）から取る
#   - 用途: 関数呼び出しの管理、深さ優先探索（DFS）、「元に戻す」機能など
#   - 操作:
#     - push(data): データをスタックの一番上（末尾）に積む
#     - pop(): データをスタックの一番上（末尾）から取り出す
#
#   【push と pop の動作イメージ（本の山）】
#
#     push("本A") → [本A]
#     push("本B") → [本A, 本B]
#     push("本C") → [本A, 本B, 本C]
#     pop() → 本C を返す   残り: [本A, 本B]   ← 最後に積んだ本Cが先に出る
#     pop() → 本B を返す   残り: [本A]
#     push("本D") → [本A, 本D]
#     pop() → 本D を返す   残り: [本A]
#     pop() → 本A を返す   残り: []
#     pop() → None を返す   ← スタックが空なので None
#
# ■ キューとスタックの図解：
#
#   キュー（FIFO）:
#     enqueue → [A, B, C, D] → dequeue
#     入口（末尾）          出口（先頭）
#     ※ A が最初に出る（先に入れたものが先に出る）
#
#   スタック（LIFO）:
#     push → [A, B, C, D] → pop
#                    ↑ 末尾から出し入れ
#     ※ D が最初に出る（最後に入れたものが先に出る）
#
# ■ キューとスタックの違いまとめ：
#     操作      | キュー (FIFO)      | スタック (LIFO)
#     ---------|--------------------|------------------
#     追加      | enqueue（末尾に）   | push（末尾に）
#     取り出し  | dequeue（先頭から）  | pop（末尾から）
#     順序      | 先入れ先出し        | 後入れ先出し
#     日常の例  | レジの行列          | 積み上げた本
#
# ■ 8-1.py（ヒープソート）での使い方：
#   ヒープから delete_min() で小さい順に取り出したデータを
#   enqueue() でキューに順番に入れていく。
#   その後 dequeue() で取り出すと、入れた順番（＝小さい順）で出てくるので、
#   結果として昇順ソートが実現される。
#
#   具体例（経度でソート）:
#     delete_min() → 経度135.5 → enqueue(135.5)
#     delete_min() → 経度135.7 → enqueue(135.7)
#     delete_min() → 経度135.9 → enqueue(135.9)
#     キュー内: [135.5, 135.7, 135.9]
#     dequeue() → 135.5  ← 最初にenqueueしたもの（最小値）が最初に出る
#     dequeue() → 135.7
#     dequeue() → 135.9
#     → 結果: 昇順に並んだ！
# ============================================================


# ============================================================
# Queue クラス（キュー / FIFO）
# ============================================================
# Pythonのリスト（配列）を内部データとして使用する。
# enqueue はリストの末尾に追加（append）、
# dequeue はリストの先頭から取り出す（pop(0)）ことで FIFO を実現する。
#
# 【内部リストの変化イメージ】
#
#   操作                    | self.list の状態
#   ----------------------|-------------------
#   初期状態               | []
#   enqueue(A)            | [A]
#   enqueue(B)            | [A, B]
#   enqueue(C)            | [A, B, C]
#   dequeue() → A を返す  | [B, C]        ← 先頭のAが取り出される
#   enqueue(D)            | [B, C, D]
#   dequeue() → B を返す  | [C, D]        ← 次の先頭Bが取り出される
#   dequeue() → C を返す  | [D]
#   dequeue() → D を返す  | []
#   dequeue() → None      | []             ← 空なのでNone
# ============================================================
class Queue:  # キュークラスの定義
    def __init__(self):
        # 内部データを格納するリストを空で初期化
        # このリストがキューの本体 — 全てのデータはここに格納される
        self.list = []

    def enqueue(self, data):
        # ============================================================
        # enqueue: キューの末尾にデータを追加するメソッド
        # ============================================================
        # 引数 data: キューに追加するデータ（8-1.pyではStopオブジェクト）
        #
        # 【何をしているか】
        #   Pythonリストの append() メソッドを使って、
        #   リストの一番後ろ（末尾）にデータを追加する。
        #   レジの行列に例えると「列の最後尾に並ぶ」操作に相当する。
        #
        # 【具体例】
        #   self.list = [A, B, C] の状態で enqueue(D) を呼ぶと:
        #   → self.list = [A, B, C, D] になる
        #   ※ Dは末尾に追加されるので、A, B, C より後に dequeue される
        #
        # 【計算量】
        #   O(1) — append() はリストの末尾に追加するだけなので一定時間で完了
        #   （リストのサイズに関係なく高速）
        #
        # 【8-1.py での使われ方】
        #   ヒープから delete_min() で取り出した最小値を enqueue() でキューに入れる。
        #   delete_min() は小さい順にデータを返すので、
        #   enqueue する順番も「小さい順」になる。
        #   → 後で dequeue() すると、入れた順番（＝小さい順）で出てくる。
        # ============================================================
        self.list.append(data)  # 末尾にデータを追加

    def dequeue(self):
        # ============================================================
        # dequeue: キューの先頭からデータを取り出して返すメソッド
        # ============================================================
        # 返り値: キューの先頭のデータ（キューが空のときは None）
        #
        # 【何をしているか】
        #   Pythonリストの pop(0) メソッドを使って、
        #   リストの先頭（インデックス0）の要素を取り出す。
        #   取り出された要素はリストから削除される。
        #   レジの行列に例えると「列の先頭の人が対応される」操作に相当する。
        #
        # 【具体例】
        #   self.list = [A, B, C, D] の状態で dequeue() を呼ぶと:
        #   → A が返される
        #   → self.list = [B, C, D] になる（Aが先頭から取り除かれる）
        #
        # 【なぜ先頭から取り出すのか】
        #   FIFO（先入れ先出し）を実現するため。
        #   enqueue() で末尾に追加し、dequeue() で先頭から取り出すことで、
        #   「最初に入れたデータが最初に出てくる」という性質が実現される。
        #
        # 【計算量について】
        #   pop(0) は先頭要素を削除した後、残りの全要素を1つ前にずらすため
        #   計算量は O(n)（n はリストの要素数）
        #
        #   なぜ O(n) なのか：
        #     pop(0) の後、[B, C, D] → B がインデックス0に、C がインデックス1に...
        #     と全要素のインデックスを1つずつ前にずらす必要があるため。
        #
        #   より効率的にするには collections.deque を使う方法がある（O(1)）
        #   ただし、この実装ではシンプルさを優先してリストを使用している
        #
        # 【8-1.py での使われ方】
        #   enqueue で入れた順番（＝小さい順）でデータを取り出し、
        #   self.list[i] に順に格納することで、昇順にソートされたリストが完成する。
        # ============================================================
        if len(self.list) == 0:
            return None  # キューが空のときはNoneを返す（ループ終了の判定に使う）
        return self.list.pop(0)  # 先頭からデータを取り出す


# ============================================================
# Stack クラス（スタック / LIFO）
# ============================================================
# Pythonのリスト（配列）を内部データとして使用する。
# push はリストの末尾に追加（append）、
# pop もリストの末尾から取り出す（pop()）ことで LIFO を実現する。
#
# 【キューとの違い】
#   キュー: 追加=末尾、取り出し=先頭 → FIFO（先入れ先出し）
#   スタック: 追加=末尾、取り出し=末尾 → LIFO（後入れ先出し）
#   ※ 追加する場所は同じ（末尾）だが、取り出す場所が違う
#
# 【内部リストの変化イメージ】
#
#   操作                    | self.list の状態
#   ----------------------|-------------------
#   初期状態               | []
#   push(A)               | [A]
#   push(B)               | [A, B]
#   push(C)               | [A, B, C]
#   pop() → C を返す      | [A, B]        ← 末尾のCが取り出される
#   push(D)               | [A, B, D]
#   pop() → D を返す      | [A, B]        ← 末尾のDが取り出される
#   pop() → B を返す      | [A]
#   pop() → A を返す      | []
#   pop() → None          | []             ← 空なのでNone
#
# ※ 第8回の課題では Stack は直接使用されていないが、
#    データ構造の基本概念としてキューとセットで定義されている。
# ============================================================
class Stack:  # スタッククラスの定義
    def __init__(self):
        # 内部データを格納するリストを空で初期化
        self.list = []

    def push(self, data):
        # スタックの一番上（リストの末尾）にデータを積むメソッド
        # append() はリストの末尾に要素を追加する — 計算量 O(1)
        #
        # 【具体例】
        #   self.list = [A, B] の状態で push(C) を呼ぶと:
        #   → self.list = [A, B, C] になる（Cが一番上に積まれる）
        self.list.append(data)

    def pop(self):
        # スタックの一番上（リストの末尾）からデータを取り出して返すメソッド
        # pop() は引数なしで呼ぶとリストの末尾の要素を取り出す
        #
        # 【具体例】
        #   self.list = [A, B, C] の状態で pop() を呼ぶと:
        #   → C が返される（一番上 = 最後に積んだもの）
        #   → self.list = [A, B] になる
        #
        # 【計算量について】
        #   pop()（引数なし）は末尾の要素を削除するだけなので計算量 O(1)
        #   キューの pop(0) と異なり、要素のずらし操作が不要なため高速
        #   → pop(0): O(n)（全要素をずらす必要あり）
        #   → pop():  O(1)（末尾を取るだけ）
        if len(self.list) == 0:
            return None  # スタックが空のときはNoneを返す
        return self.list.pop()  # 末尾からデータを取り出す
