# ============================================================
# 第8回 課題2：マージソート (Merge Sort)
# ============================================================
# このプログラムでは、京都のバス停データを「経度(lng)」の昇順に
# マージソートで並べ替えます。
#
# ■ プログラムの全体構成：
#   1. def_stop.py から Stop クラスを読み込む
#   2. def_db.py から DB クラス（親クラス）を読み込む
#   3. DBwMergeSort クラス（子クラス）で sort() をマージソートとして
#      オーバーライドする
#   4. allkyotobus_stop.dat からバス停データを読み込み、ソートして表示する
#
# ■ マージソートのアルゴリズム概要：
#   - 「分割統治法（Divide and Conquer）」に基づくソートアルゴリズム
#   - リストを再帰的に半分ずつ分割し、要素が1つになるまで分ける
#   - 分割した小さなリストを「小さい順に」マージ（統合）していく
#   - マージ時に順序が決まるため、全体がソートされる
#
#   手順：
#   1. リストを半分に分割する（再帰的に繰り返す）
#   2. 要素が1つになったら分割を止める（基底条件）
#   3. 分割したリストを「小さい順に」マージ（統合）していく
#
#   【動作イメージ】
#     [5, 3, 8, 1, 2]
#       → [5, 3] と [8, 1, 2] に分割
#       → [5] と [3] に分割 → マージ → [3, 5]
#       → [8] と [1, 2] に分割
#         → [1] と [2] → マージ → [1, 2]
#       → [8] と [1, 2] → マージ → [1, 2, 8]
#       → [3, 5] と [1, 2, 8] → マージ → [1, 2, 3, 5, 8]
#
# ■ 計算量: O(n log n)
#   - 分割の深さ: O(log n)
#     （n個の要素を毎回半分にするので、log2(n) 回で要素1つになる）
#   - 各レベルのマージ: O(n)
#     （各レベルで全要素を1回ずつ比較・移動する）
#   - 合計: O(n log n)（最悪・平均ともに同じ）
#   - 追加のメモリ領域: O(n)（マージ時に新しいリストを作成するため）
#
# ■ ヒープソート（8-1.py）との比較：
#   - 計算量: どちらも O(n log n) で同じ
#   - メモリ: ヒープソートは追加メモリ O(1)（その場でソート可能）、
#             マージソートは追加メモリ O(n)（マージ用の一時リストが必要）
#   - 安定性: マージソートは「安定ソート」（同じ値の要素の相対順序が保たれる）、
#             ヒープソートは「不安定ソート」（同じ値の要素の順序が変わる可能性）
#   - 実装: ヒープソートはヒープ構築+最小値取り出しの2段階、
#           マージソートは分割+マージの再帰的処理
#   - 実用: Pythonの組み込みソート(sorted, list.sort)は
#           マージソートを改良した「TimSort」を使用している
#   - データ構造: ヒープソートはヒープ+キュー（enqueue/dequeue）を使用、
#                 マージソートは追加のデータ構造を使わず再帰とリスト操作のみ
#
# ■ 継承（クラスの親子関係）について：
#   - DB クラスが「親クラス（基底クラス）」
#   - DBwMergeSort が「子クラス（派生クラス）」
#   - 子クラスは親クラスの全メソッド（add, display, count, is_sorted）を引き継ぐ
#   - 子クラスで sort() を再定義（オーバーライド）して、独自のソート処理を実装する
# ============================================================

from def_stop import Stop  # Stopクラスの読み込み（バス停1件分のデータを表すクラス）
from def_db import DB  # DBクラスの読み込み（データ管理の親クラス）
import time  # 実行時間を計測するためのモジュール
import sys  # システム関連の設定を変更するためのモジュール
sys.setrecursionlimit(10000)  # Pythonの再帰呼び出しの上限を10000に引き上げる（デフォルトは1000）
# マージソートは再帰的にリストを分割するため、データ数が多いと
# デフォルトの再帰上限（1000回）を超える可能性がある。
# 例: 5000件のデータ → 分割の深さは約 log2(5000) ≒ 13 だが、
#     関数呼び出しの総数はそれ以上になるため、余裕を持って設定する。


# ============================================================
# DBwMergeSort クラス（子クラス）— マージソート
# ============================================================
# DB クラスを継承し、sort() をマージソートで実装したクラス。
# マージソートは「分割統治法」に基づくアルゴリズムで、
# _merge_sort() と _merge() の2つのプライベートメソッドで構成される。
#
# ・_merge_sort(lst): リストを再帰的に半分ずつ分割し、
#                     要素が1つになったらマージしながら戻る
# ・_merge(left, right): ソート済みの2つのリストを1つにマージする
#
# 【マージソートの全体的な流れ】
#   sort() → _merge_sort(self.list) を呼び出す
#     → リストを半分に分割
#     → 左半分を _merge_sort() で再帰的にソート
#     → 右半分を _merge_sort() で再帰的にソート
#     → ソート済みの左右を _merge() で統合
#     → 統合結果を self.list に代入（上書き）
#
# 【ヒープソート（8-1.py）との実装上の違い】
#   ヒープソート: ヒープとキューという2つのデータ構造を使い、
#                 build_heap → delete_min → enqueue → dequeue の流れ
#   マージソート: 追加のデータ構造は不要、再帰とリスト操作のみで完結
#
# 【マージソートが追加のデータ構造を使わずに済む理由】
#   ヒープソートでは「最小値を取り出す順番」をキューで保持する必要があった。
#   マージソートでは、再帰的に分割→マージする過程で自然と順番が決まるため、
#   キューやスタックなどの追加のデータ構造が不要。
# ============================================================
class DBwMergeSort(DB):  # マージソート用のクラス（DBを継承）

    def sort(self):
        # sort() は親クラス DB の sort()（pass だけ）をオーバーライドしている
        # self.list 全体を _merge_sort() に渡し、ソート済みリストで上書きする
        # _merge_sort() は元のリストを変更せず、新しいソート済みリストを返す
        self.list = self._merge_sort(self.list)  # self.list を マージソートで並べ替えた結果で上書きする

    def _merge_sort(self, lst):
        # -------------------------------------------------------
        # _merge_sort: リストを再帰的に分割し、マージして返す
        # -------------------------------------------------------
        # 引数 lst: ソート対象のリスト（Stopオブジェクトのリスト）
        # 返り値: ソート済みのリスト（新しいリストとして返す）
        #
        # 【再帰の仕組み】
        #   再帰（recursion）とは、関数が自分自身を呼び出す手法。
        #   マージソートでは、リストを半分に分割する処理を再帰的に行う。
        #   分割は要素が1つになるまで続き、その後マージしながら戻っていく。
        #
        # 【再帰の動作例: [5, 3, 8, 1] の場合】
        #   _merge_sort([5,3,8,1])
        #     ├─ _merge_sort([5,3])        ← 左半分
        #     │    ├─ _merge_sort([5])     ← 要素1つ → そのまま返す
        #     │    ├─ _merge_sort([3])     ← 要素1つ → そのまま返す
        #     │    └─ _merge([5],[3]) → [3,5]  ← マージ
        #     ├─ _merge_sort([8,1])        ← 右半分
        #     │    ├─ _merge_sort([8])     ← 要素1つ → そのまま返す
        #     │    ├─ _merge_sort([1])     ← 要素1つ → そのまま返す
        #     │    └─ _merge([8],[1]) → [1,8]  ← マージ
        #     └─ _merge([3,5],[1,8]) → [1,3,5,8]  ← 最終マージ
        #
        # 【再帰の実行順序（呼び出し → 戻りの流れ）】
        #   1. _merge_sort([5,3,8,1]) を呼び出す
        #   2.   左半分: _merge_sort([5,3]) を呼び出す
        #   3.     左半分: _merge_sort([5]) → [5] を返す
        #   4.     右半分: _merge_sort([3]) → [3] を返す
        #   5.     _merge([5],[3]) → [3,5] を返す
        #   6.   右半分: _merge_sort([8,1]) を呼び出す
        #   7.     左半分: _merge_sort([8]) → [8] を返す
        #   8.     右半分: _merge_sort([1]) → [1] を返す
        #   9.     _merge([8],[1]) → [1,8] を返す
        #  10.   _merge([3,5],[1,8]) → [1,3,5,8] を返す
        # -------------------------------------------------------

        # 基底条件（Base Case）: 再帰を止める条件
        # 要素が1つ以下のリストは、それ自体が既にソート済みなのでそのまま返す
        # これがないと無限再帰になり、RecursionError（スタックオーバーフロー）が発生する
        if len(lst) <= 1:  # 要素が0個または1個の場合
            return lst  # 分割の必要がないのでそのまま返す

        # リストを半分に分割するための中央のインデックスを計算
        # // は整数除算（小数点以下を切り捨てる演算子）
        # 例: len(lst)=5 → mid=2, len(lst)=4 → mid=2, len(lst)=3 → mid=1
        mid = len(lst) // 2  # 中央のインデックスを計算

        # スライス（lst[:mid]）を使ってリストを分割する
        # lst[:mid] → 先頭から mid-1 番目までの要素（左半分）
        # lst[mid:] → mid 番目から末尾までの要素（右半分）
        # 例: lst=[5,3,8,1,2], mid=2
        #     lst[:2] → [5,3]（左半分）
        #     lst[2:] → [8,1,2]（右半分）
        # ※ スライスは元のリストを変更せず、新しいリストを作成する
        left = self._merge_sort(lst[:mid])    # 左半分を再帰的にソート
        right = self._merge_sort(lst[mid:])   # 右半分を再帰的にソート

        # ソート済みの左右のリストを _merge() で1つに統合して返す
        return self._merge(left, right)  # ソート済みの左右をマージして返す

    def _merge(self, left, right):
        # -------------------------------------------------------
        # _merge: ソート済みの2つのリストを1つのソート済みリストにマージする
        # -------------------------------------------------------
        # 引数 left: ソート済みの左リスト（Stopオブジェクトのリスト）
        # 引数 right: ソート済みの右リスト（Stopオブジェクトのリスト）
        # 返り値: マージされたソート済みリスト
        #
        # 【マージの動作イメージ】
        #   left = [1, 3, 5],  right = [2, 4, 6] の場合:
        #
        #   ステップ1: left[0]=1 と right[0]=2 を比較 → 1 が小さい → result=[1]
        #   ステップ2: left[1]=3 と right[0]=2 を比較 → 2 が小さい → result=[1,2]
        #   ステップ3: left[1]=3 と right[1]=4 を比較 → 3 が小さい → result=[1,2,3]
        #   ステップ4: left[2]=5 と right[1]=4 を比較 → 4 が小さい → result=[1,2,3,4]
        #   ステップ5: left[2]=5 と right[2]=6 を比較 → 5 が小さい → result=[1,2,3,4,5]
        #   ステップ6: left は終了 → right の残り [6] を追加 → result=[1,2,3,4,5,6]
        #
        # 【なぜこの方法で正しくソートできるのか】
        #   left と right はそれぞれ既にソート済みなので、
        #   両方の先頭を比較するだけで「全体の中で最も小さい要素」を見つけられる。
        #   これを繰り返すことで、全体がソート済みのリストになる。
        #
        # 【ヒープソートのenqueue/dequeueとの対比】
        #   ヒープソート: delete_min→enqueue→dequeueで順番を保持
        #   マージソート: 2つのソート済みリストの先頭比較で順番を決定
        #   どちらも「小さい方を先に選ぶ」という点は共通している。
        # -------------------------------------------------------

        result = []  # マージ結果を格納する新しいリスト
        i = 0  # left のインデックス（左リストの現在の参照位置）
        j = 0  # right のインデックス（右リストの現在の参照位置）

        # --- while ループ: 両方のリストに要素が残っている間、比較・追加を繰り返す ---
        # i < len(left): 左リストにまだ未処理の要素がある
        # j < len(right): 右リストにまだ未処理の要素がある
        # 両方の条件が True（and）の間だけループする
        # どちらか一方のリストが空になったらループを抜ける
        while i < len(left) and j < len(right):  # 両方のリストに要素が残っている間
            # left[i] と right[j] の経度(lng)を比較し、小さい方を result に追加
            #
            # 【重要: <= を使う理由（安定ソートの保証）】
            #   < ではなく <= を使うことで「安定ソート」になる。
            #   安定ソート = 同じ値を持つ要素の元の順序が保たれるソート。
            #   例: left=[A(lng=3), B(lng=5)], right=[C(lng=3), D(lng=7)]
            #       <= で A(lng=3) と C(lng=3) を比較すると、A が left にあるので
            #       <= により A が先に result に入る → 元の順序 A→C が保たれる。
            #   もし < を使うと、C が先に入る場合があり、順序が崩れてしまう。
            if left[i].lng <= right[j].lng:  # 左の経度が右以下なら左を追加
                result.append(left[i])  # 左リストの現在の要素を result に追加
                i += 1  # 左リストのインデックスを1つ進める
            else:  # 右の経度が小さい場合
                result.append(right[j])  # 右リストの現在の要素を result に追加
                j += 1  # 右リストのインデックスを1つ進める

        # --- 残りの要素を追加 ---
        # while ループを抜けた時点で、left か right のどちらかは全要素が処理済み。
        # もう一方のリストに残っている要素は、全て result の末尾に追加する。
        # 残っている要素は既にソート済みで、かつ result の最後の要素より大きいので、
        # そのまま追加するだけで正しい順序になる。
        #
        # extend() はリストの末尾に別のリストの全要素を追加するメソッド。
        # append() は要素を1つだけ追加するが、extend() は複数要素をまとめて追加できる。
        # left[i:] → left の i 番目以降の全要素（空リストの場合もある）
        # right[j:] → right の j 番目以降の全要素（空リストの場合もある）
        result.extend(left[i:])  # 左リストの残りを全て追加（左が先に空になった場合は空リスト）
        result.extend(right[j:])  # 右リストの残りを全て追加（右が先に空になった場合は空リスト）
        return result  # マージ済みのソート済みリストを返す


# ============================================================
# メインの実行部分
# ============================================================
if __name__ == '__main__':  # モジュールとしてインポートされるときは実行しない

    # --- データの読み込み ---
    fi = open('allkyotobus_stop.dat', 'r', encoding='utf-8')
    lines = fi.readlines()

    db = DBwMergeSort()  # マージソート用のDBインスタンスを作成

    # --- データの格納 ---
    for line in lines:
        line = line.rstrip()  # 行末の改行文字(\n)を除去
        items = line.split(' ')  # 1行を半角スペースで区切ってitemsリストに代入
        db.add(Stop(items[0], items[1], float(items[2]), float(items[3]), items[4:]))

    # --- ソートの実行と時間計測 ---
    start = time.time()  # ソート前の時間を記録
    db.sort()  # データのソートを実行（マージソートが呼ばれる）
    elapsed_time = time.time() - start  # 実行時間を計測

    # --- 結果の表示 ---
    db.display()  # ソート済みの全データを表示

    if db.is_sorted():  # ソートが正しく完了したか検証
        print('ソートが完了しました！ {}は{}秒かかりました。'.format(db.__class__.__name__, elapsed_time))

    fi.close()  # ファイルを閉じる
