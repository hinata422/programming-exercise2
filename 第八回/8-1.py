# ============================================================
# 第8回 課題1：ヒープソート (Heap Sort)
# ============================================================
# このプログラムでは、京都のバス停データを「経度(lng)」の昇順に
# ヒープソートで並べ替えます。
#
# ■ プログラムの全体構成：
#   1. def_stop.py から Stop クラスを読み込む（バス停1件分のデータ）
#   2. def_heap.py から Heap クラスを読み込む（最小ヒープのデータ構造）
#   3. def_queue_stack.py から Queue クラスを読み込む（キュー / FIFO）
#   4. def_db.py から DB クラス（親クラス）を読み込む
#   5. DBwHeapSort クラス（子クラス）で sort() をヒープソートとして実装する
#   6. allkyotobus_stop.dat からバス停データを読み込み、ソートして表示する
#
#
# ================================================================
# ■ ヒープって何？（ヒープソートを理解するための前提知識）
# ================================================================
#
# ヒープ = 「完全二分木」+「親 <= 子のルール」を持つデータ構造
#
# 【イメージ】
#   ヒープは「木」の形をしたデータの入れ物。
#   一番上（根 / ルート）に常に「最小値」がある。
#
#          10          ← 根 = 全体の最小値!
#         /  \
#       20    30       ← 親(10)より大きい → OK
#      /  \
#    40    50          ← 親(20)より大きい → OK
#
# 【なぜヒープを使うのか — ソートに便利な理由】
#   ヒープでは根が常に最小値なので:
#     1回目: 根(最小値)を取り出す → 全体で1番小さいデータが得られる
#     2回目: 根(次の最小値)を取り出す → 全体で2番目に小さいデータが得られる
#     3回目: ...
#     → これを繰り返すと「小さい順」にデータが取り出せる → ソート完成！
#
# 【普通のリストとの違い】
#   普通のリスト [50, 20, 40, 10, 30] で最小値を探す → 全部見る必要あり → O(n)
#   ヒープ → 根を見るだけ → O(1)、取り出し後の修復も O(log n)
#   → 大量データのソートでヒープを使うと高速！
#
# 【ヒープの詳しい仕組み】
#   → def_heap.py に「木構造とは」「二分木とは」からの詳細な解説あり
#
#
# ================================================================
# ■ ヒープソートのアルゴリズム（全体の流れ）
# ================================================================
#
# ヒープソートは「ヒープ」と「キュー」という2つのデータ構造を組み合わせて
# ソートを実現するアルゴリズム。
#
# 【一言で言うと】
#   ヒープから最小値を順番に取り出して、キューに入れて、リストに戻す。
#
# ■ ヒープソートのアルゴリズム（全体の流れ）：
#
#   【ステップ1】build_heap: self.list のデータからヒープを構築する
#     - 無秩序なリストを、最小ヒープ（親 <= 子）の条件を満たすように変換する
#     - 計算量: O(n)
#
#   【ステップ2】delete_min + enqueue: ヒープから最小値を取り出し、キューに入れる
#     - delete_min() はヒープの根（最小値）を取り出す
#     - 取り出した最小値を enqueue() でキューの末尾に追加する
#     - ヒープが空になるまで繰り返す
#     - delete_min() は毎回「残りの中で最小のもの」を返すので、
#       enqueue する順番は自動的に「小さい順」になる
#     - 計算量: delete_min を n 回 → 各 O(log n) → 合計 O(n log n)
#
#   【ステップ3】dequeue: キューからデータを取り出し、リストに再格納する
#     - dequeue() はキューの先頭からデータを取り出す（FIFO: 先入れ先出し）
#     - enqueue した順番（＝小さい順）でデータが出てくる
#     - 取り出したデータを self.list に順に格納すれば昇順ソートが完成
#     - 計算量: dequeue を n 回 → 各 O(n) → 合計 O(n^2)
#       ※ pop(0) を使っているため。実用上は O(n log n) が支配的
#
# ■ なぜキュー（Queue）を使うのか：
#
#   キューは FIFO（先入れ先出し）構造:
#     - 最初に enqueue したデータが、最初に dequeue される
#
#   delete_min() は値が小さい順にデータを返すので:
#     1回目の delete_min() → 経度 135.5（最小）→ enqueue
#     2回目の delete_min() → 経度 135.7（2番目に小さい）→ enqueue
#     3回目の delete_min() → 経度 135.9（3番目に小さい）→ enqueue
#     ...
#
#   この時点でキューの中身: [135.5, 135.7, 135.9, ...]
#     ※ 135.5 が最初に入ったので先頭にいる
#
#   dequeue すると先入れ先出しなので:
#     1回目の dequeue() → 135.5（最初に入れたもの）
#     2回目の dequeue() → 135.7
#     3回目の dequeue() → 135.9
#     ...
#
#   → 小さい順にデータが出てくる → 昇順ソートが実現される！
#
#   【もしスタック（LIFO: 後入れ先出し）を使ったら？】
#     dequeue の代わりに pop すると:
#     最後に入れたもの（= 最大値）が最初に出てくる → 降順になってしまう
#     → キュー（FIFO）を使うのが正解
#
# ■ 具体的なソートの流れ（イメージ）：
#
#   元データ: [経度135.9, 経度135.5, 経度135.7, 経度136.0, 経度135.3]
#
#   ステップ1: build_heap でヒープ構築
#           135.3         ← 根 = 最小値
#          /     \
#       135.5   135.7
#       /   \
#    136.0  135.9
#
#   ステップ2: delete_min + enqueue を繰り返す
#     delete_min() → 135.3 → enqueue → キュー: [135.3]
#     delete_min() → 135.5 → enqueue → キュー: [135.3, 135.5]
#     delete_min() → 135.7 → enqueue → キュー: [135.3, 135.5, 135.7]
#     delete_min() → 135.9 → enqueue → キュー: [135.3, 135.5, 135.7, 135.9]
#     delete_min() → 136.0 → enqueue → キュー: [135.3, 135.5, 135.7, 135.9, 136.0]
#     delete_min() → None → ループ終了（ヒープが空）
#
#   ステップ3: dequeue でリストに再格納
#     dequeue() → 135.3 → list[0] = 135.3
#     dequeue() → 135.5 → list[1] = 135.5
#     dequeue() → 135.7 → list[2] = 135.7
#     dequeue() → 135.9 → list[3] = 135.9
#     dequeue() → 136.0 → list[4] = 136.0
#     dequeue() → None → ループ終了（キューが空）
#
#   結果: [135.3, 135.5, 135.7, 135.9, 136.0] → 昇順にソート完了！
#
# ■ 計算量: O(n log n)
#   - build_heap: O(n)
#   - delete_min を n 回: 各 O(log n) → 合計 O(n log n)
#   - enqueue を n 回: 各 O(1) → 合計 O(n)
#   - dequeue を n 回: 各 O(n)（pop(0)のため）→ 合計 O(n^2)
#     ※ 全体では delete_min の O(n log n) が支配的
#
# ■ 穴埋め問題のポイント：
#   - delete_min() の返り値が None → ヒープが空
#   - dequeue() の返り値が None → キューが空
#   - while ループの条件: min is not None（None でない間ループを続ける）
# ============================================================

from def_stop import Stop  # Stopクラスの読み込み（バス停1件分のデータを表すクラス）
from def_heap import Heap  # Heapクラスの読み込み（最小ヒープのデータ構造）
from def_queue_stack import Queue  # Queueクラスの読み込み（キュー / FIFO構造）
from def_db import DB  # DBクラスの読み込み（データ管理の親クラス）
import time  # 実行時間を計測するためのモジュール


# ============================================================
# DBwHeapSort クラス（子クラス）— ヒープソート
# ============================================================
# DB クラスを継承し、sort() をヒープソートで実装したクラス。
#
# 【ヒープソートが使う2つのデータ構造】
#
#   1. ヒープ（Heap）— 最小値を効率的に取り出すためのデータ構造
#      - build_heap(): リストからヒープを構築 → O(n)
#      - delete_min(): 最小値を取り出す → O(log n)
#
#   2. キュー（Queue）— 取り出した順番を保持するためのデータ構造
#      - enqueue(): キューの末尾にデータを追加 → O(1)
#      - dequeue(): キューの先頭からデータを取り出す → O(n)
#      ※ FIFO（先入れ先出し）なので、入れた順番がそのまま出る順番になる
#
# 【他のソートとの実装上の違い】
#   - マージソート（8-2.py）: 再帰とリスト操作のみで完結、追加データ構造不要
#   - クイックソート（8-3.py）: 再帰とスワップのみで完結、追加データ構造不要
#   - ヒープソート（この課題）: ヒープ + キューという2つのデータ構造を使用
# ============================================================
class DBwHeapSort(DB):  # ヒープソート用のクラス（DBを継承）
    def __init__(self):
        super().__init__()  # 親クラスの__init__()の呼び出し（self.list = [] を実行）
        self.heap = Heap()  # ヒープのインスタンスを生成（最小値を取り出すために使う）
        self.queue = Queue()  # キューのインスタンスを生成（取り出した順序を保持するために使う）

    def sort(self):
        # ============================================================
        # sort: ヒープソートを実行するメソッド
        # ============================================================
        # 親クラス DB の sort()（pass だけ）をオーバーライドしている。
        # 3つのステップで昇順ソートを実現する。
        # ============================================================

        # ----------------------------------------------------------
        # ステップ1: self.list のデータからヒープを構築
        # ----------------------------------------------------------
        # build_heap() は self.list の全要素を使って最小ヒープを一括構築する。
        # 構築後、ヒープの根（インデックス1）には最小値が来る。
        # 計算量: O(n)
        self.heap.build_heap(self.list)  # self.listからヒープを構築

        # ----------------------------------------------------------
        # ステップ2: ヒープから最小値を繰り返し取り出し、キューに入れる
        # ----------------------------------------------------------
        # delete_min() はヒープの根（最小値）を取り出して返す。
        # 取り出し後、ヒープは自動的に次に小さい値が根に来るよう修復される。
        # ヒープが空になると None を返す。
        #
        # 穴埋め: delete_min() — ヒープの根（最小値）を取り出すメソッド
        min = self.heap.delete_min()  # ヒープの根を取得する

        # None — delete_min()はヒープが空のときNoneを返す
        while min is not None:  # ヒープの根が取得できる限り繰り返す
            # ----------------------------------------------------------
            # enqueue(min): キューの末尾にデータを追加する操作
            # ----------------------------------------------------------
            # 【enqueue の役割】
            #   delete_min() で取り出したデータ（最小値）をキューの末尾に追加する。
            #
            # 【なぜ enqueue するのか】
            #   delete_min() は毎回「残りの中で最小のもの」を返す。
            #   つまり、1回目は全体の最小値、2回目は2番目に小さい値...となる。
            #   これを enqueue すると、キューの中身は小さい順に並ぶ。
            #
            # 【enqueue の内部動作】
            #   self.queue.list に append(min) するだけ。
            #   例: self.queue.list = [135.3, 135.5] の状態で enqueue(135.7) すると
            #       self.queue.list = [135.3, 135.5, 135.7] になる
            #
            # 【キュー（FIFO）がソートに適している理由】
            #   キューは「先入れ先出し」なので:
            #   - 最初に enqueue した 135.3（最小）が、最初に dequeue される
            #   - 次に enqueue した 135.5 が、次に dequeue される
            #   → dequeue する順番 = enqueue した順番 = 小さい順 → 昇順ソート！
            self.queue.enqueue(min)

            # 次の最小値を取り出す（ヒープ内部で shift_down が走り、次の最小値が根に来る）
            min = self.heap.delete_min()  # ヒープの根を取得する

        # ----------------------------------------------------------
        # ステップ3: キューからデータを取り出し、リストに再格納
        # ----------------------------------------------------------
        i = 0  # self.list に再格納するためのインデックス

        # dequeue(): キューの先頭からデータを取り出す操作
        # 【dequeue の内部動作】
        #   self.queue.list の先頭（インデックス0）を pop(0) で取り出す。
        #   例: self.queue.list = [135.3, 135.5, 135.7] で dequeue() すると
        #       135.3 が返され、self.queue.list = [135.5, 135.7] になる
        #
        # enqueue した順番（＝小さい順）で取り出されるため、
        # self.list に順に格納すれば昇順に並んだリストが完成する。
        # キューが空のときは None を返す → ループ終了の条件になる。
        min = self.queue.dequeue()

        # 穴埋め: None — dequeue()はキューが空のときNoneを返す
        while min is not None:  # キューからデータを取り出せる限り繰り返す
            self.list[i] = min  # 取り出したデータをDBのリストに格納する
            # i=0 → 最小値, i=1 → 2番目に小さい値, ... と順に格納される
            i += 1
            # 次のデータをキューの先頭から取り出す（FIFO順＝小さい順）
            min = self.queue.dequeue()


# ============================================================
# メインの実行部分
# ============================================================
if __name__ == '__main__':  # モジュールとしてインポートされるときは実行しない

    # --- データの読み込み ---
    fi = open('allkyotobus_stop.dat', 'r', encoding='utf-8')
    lines = fi.readlines()  # ファイル全体を行ごとのリストとして読み込む

    db = DBwHeapSort()  # ヒープソート用のDBインスタンスを作成

    # --- データの格納 ---
    for line in lines:
        line = line.rstrip()  # 行末の改行文字(\n)を除去
        items = line.split(' ')  # 1行を半角スペースで区切ってitemsリストに代入
        # items[0] → ID, items[1] → 名前, items[2] → 緯度, items[3] → 経度
        # items[4:] → 5番目以降の全要素（路線名のリスト）
        db.add(Stop(items[0], items[1], float(items[2]), float(items[3]), items[4:]))

    # --- ソートの実行と時間計測 ---
    start = time.time()  # ソート前の時間を取得
    db.sort()  # データのソートを実行（ヒープソートが呼ばれる）
    elapsed_time = time.time() - start  # 実行時間を計算

    # --- 結果の表示 ---
    db.display()  # ソート済みの全データを表示

    if db.is_sorted():  # ソートが正しく完了したか検証
        print('ソートが完了しました！ {}は{}秒かかりました。'.format(db.__class__.__name__, elapsed_time))

    fi.close()  # ファイルを閉じる
