# ============================================================
# Heap クラスの定義（最小ヒープ / Min-Heap）
# ============================================================
# このファイルでは、最小ヒープ（Min-Heap）のデータ構造を定義する。
#
#
# ================================================================
# ■ そもそも「ヒープ」って何？ — ゼロからの解説
# ================================================================
#
# ヒープを理解するには、まず「木構造」「二分木」を順番に知る必要がある。
#
# ────────────────────────────────────────────
# ステップ1: 木構造（Tree）とは
# ────────────────────────────────────────────
#
#   木構造とは、データを「親子関係」で階層的に管理するデータ構造。
#   ファイルシステム（フォルダの中にフォルダがある構造）と同じイメージ。
#
#   身近な例:
#     - パソコンのフォルダ構造（Documents の中に Work や Photos がある）
#     - 家系図（祖父母→父母→子供 という階層）
#     - 会社の組織図（社長→部長→課長→平社員）
#
#   木構造の用語:
#     - ノード（節）: データを格納する1つ1つの要素
#     - 根（ルート）: 木の一番上にあるノード（親を持たない唯一のノード）
#     - 葉（リーフ）: 子を持たないノード（木の一番下）
#     - 親（parent）: あるノードの1つ上のノード
#     - 子（child）: あるノードの1つ下のノード
#     - 深さ（depth）: 根からそのノードまでの距離（根の深さは0）
#
#   例: フォルダ構造を木構造で表す
#
#          Documents          ← 根（ルート）深さ0
#          /      \
#       Work     Photos       ← 深さ1
#       /  \        \
#    report  slides  trip     ← 葉（リーフ）深さ2
#
# ────────────────────────────────────────────
# ステップ2: 二分木（Binary Tree）とは
# ────────────────────────────────────────────
#
#   二分木とは、各ノードが「最大2つまで」の子を持つ木構造。
#   子は「左の子」と「右の子」の2つに分かれる。
#
#       A           ← 根
#      / \
#     B   C         ← Aの左の子=B、右の子=C
#    / \
#   D   E           ← Bの左の子=D、右の子=E
#
#   「二分」= 「2つに分かれる」という意味。
#   各ノードから出る枝が最大2本なので「二分木」と呼ぶ。
#
# ────────────────────────────────────────────
# ステップ3: 完全二分木（Complete Binary Tree）とは
# ────────────────────────────────────────────
#
#   完全二分木とは、以下の条件を満たす二分木:
#     - 最下段を除く全ての段が完全に埋まっている
#     - 最下段は左詰めで埋まっている
#
#   ◯ 完全二分木の例:
#
#          10           10           10
#         /  \         /  \         /  \
#       20    30     20    30     20    30
#      /  \         /             /  \   /
#    40    50     40            40  50 60
#
#   × 完全二分木でない例:
#
#          10           10
#         /  \         /  \
#       20    30     20    30
#         \               \
#          50             60
#     （左が空なのに     （最下段が左詰め
#      右に子がある）     でない）
#
#   【完全二分木の重要な性質】
#     完全二分木は「配列（リスト）」で表現できる。
#     普通の木構造はポインタ（参照）を使って親子をつなぐ必要があるが、
#     完全二分木は形が規則的なので、配列のインデックスだけで
#     親子関係を計算できる。→ メモリ効率が良い！
#
# ────────────────────────────────────────────
# ステップ4: ヒープ（Heap）とは — ようやく本題！
# ────────────────────────────────────────────
#
#   ヒープとは、「完全二分木」に「親子の大小関係のルール」を加えたもの。
#
#   ■ 最小ヒープ（Min-Heap）— このプログラムで使うもの:
#     ルール: 「親の値 <= 子の値」が全てのノードで成り立つ
#     → 根（一番上）に常に「最小値」が来る
#
#   ■ 最大ヒープ（Max-Heap）:
#     ルール: 「親の値 >= 子の値」が全てのノードで成り立つ
#     → 根（一番上）に常に「最大値」が来る
#
#   【最小ヒープの例】
#
#          10          ← 根 = 全体の最小値!
#         /  \
#       20    30       ← どちらも親(10)以上 → OK
#      /  \
#    40    50          ← どちらも親(20)以上 → OK
#
#   ※ 注意: 左の子と右の子の大小関係にルールはない！
#     上の例で 20 < 30 だが、逆（30 < 20）でもOK。
#     ルールは「親 <= 子」だけ。兄弟同士の大小は問わない。
#
#   【これはヒープではない例】
#
#          10
#         /  \
#       30    20
#      /  \
#    25    50
#
#     → 30(親) > 25(子) なのでヒープ条件違反！
#       （親が子より大きい箇所がある）
#
#   【日常の例えで理解するヒープ】
#
#     会社の給料制度に例えると:
#     「上司の給料は必ず部下の給料以上」というルールがある組織図。
#     → 社長（根）が必ず最も低い給料になる（最小ヒープの場合）
#     → ただし、同じ上司の部下同士の給料は自由
#
#     部長A(給料50万) と 部長B(給料40万) の順番は問わない。
#     重要なのは「社長(給料30万) <= 部長A(50万)」かつ
#     「社長(給料30万) <= 部長B(40万)」という親子関係だけ。
#
# ────────────────────────────────────────────
# ステップ5: なぜヒープが便利なのか
# ────────────────────────────────────────────
#
#   ヒープの最大の利点: 「最小値を高速に取り出せる」こと
#
#   【普通のリストで最小値を探す場合】
#     リスト: [50, 20, 40, 10, 30]
#     → 先頭から全部調べる必要がある → O(n)
#     → 10 が最小値だとわかるまでに全要素を見る必要がある
#
#   【ヒープで最小値を取り出す場合】
#     ヒープ:
#          10         ← 根を見るだけ！ O(1)で最小値がわかる
#         /  \
#       20    40
#      /  \
#    50    30
#
#     → 根（一番上）を見れば最小値が即座にわかる
#     → 取り出し後のヒープ修復も O(log n) で高速
#
#   【計算量の比較】
#     操作            | 普通のリスト | ヒープ
#     --------------|------------|----------
#     最小値の取得    | O(n)       | O(1)
#     最小値の取り出し | O(n)       | O(log n)
#     データの挿入    | O(1)       | O(log n)
#
#     n=1000 の場合:
#       リストで最小値取り出し: 最大1000回の比較
#       ヒープで最小値取り出し: 最大 log2(1000) ≒ 10回の操作
#       → ヒープの方が圧倒的に速い！
#
#   【ヒープソート（8-1.py）でヒープを使う理由】
#     ヒープに全データを入れて、delete_min() を繰り返すと
#     小さい順にデータが出てくる。
#     これを enqueue でキューに入れて、dequeue で取り出せば
#     昇順に並んだリストが完成する。
#
#
# ================================================================
# ■ リストによる完全二分木の表現（ヒープの内部実装）
# ================================================================
#
# ヒープは概念的には「木」だが、実際にはPythonのリスト（配列）で実装する。
# 完全二分木の形が規則的なので、リストのインデックスだけで親子関係が計算できる。
#
#   - インデックス 0 にはダミー値（0）を入れ、実際のデータはインデックス 1 から格納する
#   - これにより、以下の簡潔な親子関係の計算が可能になる：
#     - 親ノードのインデックス: i // 2
#     - 左の子ノードのインデックス: i * 2
#     - 右の子ノードのインデックス: i * 2 + 1
#
#   【なぜインデックス0をダミーにするのか】
#     もしインデックス0からデータを入れると、親子関係の計算式が複雑になる：
#       - 親: (i - 1) // 2
#       - 左の子: 2 * i + 1
#       - 右の子: 2 * i + 2
#     インデックス1から始めると式がシンプルになり、コードも読みやすくなる。
#
#   【リストとツリーの対応関係】
#
#     リスト: [0, 10, 20, 30, 40, 50]
#              ↑   ↑   ↑   ↑   ↑   ↑
#            ダミー idx1 idx2 idx3 idx4 idx5
#
#     ツリー表現:
#          10          ← インデックス1（根 / ルート）= 最小値
#         /  \
#       20    30       ← インデックス2, 3
#      /  \
#    40    50          ← インデックス4, 5
#
#     親子関係の計算例:
#       インデックス4(値=40)の親 → 4 // 2 = 2(値=20) ✓
#       インデックス2(値=20)の左子 → 2 * 2 = 4(値=40) ✓
#       インデックス2(値=20)の右子 → 2 * 2 + 1 = 5(値=50) ✓
#       インデックス3(値=30)の親 → 3 // 2 = 1(値=10) ✓
#
#     インデックス | 値  | 親(i//2) | 左子(i*2) | 右子(i*2+1)
#     ------------|-----|---------|----------|------------
#     1           | 10  | なし     | 2(=20)   | 3(=30)
#     2           | 20  | 1(=10)  | 4(=40)   | 5(=50)
#     3           | 30  | 1(=10)  | なし      | なし
#     4           | 40  | 2(=20)  | なし      | なし
#     5           | 50  | 2(=20)  | なし      | なし
#
#
# ================================================================
# ■ 最小ヒープの性質（ヒープ条件）
# ================================================================
#
#   全てのノードについて「親の値 <= 子の値」が成り立つ
#   → 根（インデックス1）には常に全体の最小値がある
#   → だから delete_min() で根を取り出せば O(1) で最小値が得られる
#      （ただし、その後のヒープ修復に O(log n) かかる）
#
#
# ================================================================
# ■ 主なメソッド
# ================================================================
#
#   - insert(data)    : ヒープにデータを挿入する — O(log n)
#   - shift_up(num)   : 挿入後にヒープ条件を回復する（上方向に移動）
#   - delete_min()    : 最小値（根）を取り出して返す — O(log n)
#   - shift_down(num) : 削除後にヒープ条件を回復する（下方向に移動）
#   - build_heap(lst) : 既存のリストからヒープを一括構築する — O(n)
#   - is_heap()       : ヒープ条件が満たされているか検証する — O(n)
#
# ■ 比較に使うキー：
#   - このヒープでは、各要素の経度(lng)を比較キーとして使用する
#   - つまり、経度が最も小さい（最も西にある）データが根に来る
# ============================================================
import sys
sys.setrecursionlimit(10000)  # 再帰呼び出しの上限を10000に拡大（大量データ対応）


class Heap:  # 最小ヒープクラスの定義
    def __init__(self):
        # インデックス0にダミー値(0)を入れて初期化
        # これにより、インデックス1から実データを格納でき、
        # 親子関係の計算（i//2, i*2, i*2+1）がシンプルになる
        self.list = [0]  # インデックス0はダミー
        self.size = 0  # ヒープに格納されているデータの件数

    def insert(self, data):
        # ============================================================
        # insert: ヒープにデータを1件挿入するメソッド — O(log n)
        # ============================================================
        # 引数 data: 挿入するデータ（Stopオブジェクト）
        #
        # 【手順】
        #   1. リストの末尾にデータを追加する（完全二分木の最後の位置に入れる）
        #   2. サイズを1増やす
        #   3. shift_up() で追加した要素を適切な位置まで上に移動させる
        #      → 親よりも値が小さい場合、親と交換しながら上に登っていく
        #
        # 【具体例: 経度15 を挿入する場合】
        #
        #   挿入前のヒープ:
        #          10
        #         /  \
        #       20    30
        #
        #   ステップ1: 末尾に15を追加
        #          10
        #         /  \
        #       20    30
        #      /
        #    15   ← ここに追加
        #
        #   ステップ2: shift_up() で修復
        #   15(子) < 20(親) → 交換!
        #          10
        #         /  \
        #       15    30
        #      /
        #    20
        #
        #   15(子) > 10(親) → 交換不要、終了
        #   最終結果:
        #          10
        #         /  \
        #       15    30
        #      /
        #    20
        # ============================================================
        self.list.append(data)
        self.size += 1
        self.shift_up(self.size)  # 末尾に追加した要素を上方向に移動してヒープ条件を回復

    def shift_up(self, num):
        # ============================================================
        # shift_up: ヒープの「上方向への修復」を行う再帰メソッド — O(log n)
        # ============================================================
        # 引数 num: 修復を開始するノードのインデックス
        #
        # 【何をしているか】
        #   インデックス num の要素が親（num // 2）より小さい場合、
        #   親と交換して上に移動させる。
        #   これを根（インデックス1）に到達するか、
        #   親の方が小さくなるまで再帰的に繰り返す。
        #
        # 【なぜ必要か】
        #   insert() で末尾に追加した要素が親より小さい場合、
        #   ヒープ条件（親 <= 子）に違反する。
        #   違反を解消するために、小さい要素を上に「浮き上がらせる」。
        #
        # 【具体例: インデックス4の要素(経度15)を上に移動】
        #
        #   リスト: [0, 10, 20, 30, 15]
        #   ツリー表現:
        #          10(idx1)
        #         /  \
        #     20(idx2)  30(idx3)
        #      /
        #   15(idx4)  ← num=4
        #
        #   num=4, 親=4//2=2
        #   list[2].lng=20 > list[4].lng=15 → 条件を満たすので交換！
        #
        #   交換後: [0, 10, 15, 30, 20]
        #          10(idx1)
        #         /  \
        #     15(idx2)  30(idx3)
        #      /
        #   20(idx4)
        #
        #   次に num=2, 親=2//2=1
        #   list[1].lng=10 > list[2].lng=15 → False → 交換不要、終了
        #
        # 【計算量】
        #   最悪の場合、葉から根まで移動する → 木の高さ = O(log n)
        # ============================================================
        # 条件: num > 1（根でない）かつ 親の経度 > 子の経度（ヒープ条件違反）
        if num > 1 and self.list[num // 2].lng > self.list[num].lng:
            # 親と子を交換（Pythonの多重代入によるスワップ）
            self.list[num], self.list[num // 2] = self.list[num // 2], self.list[num]
            # 交換後、さらに上（親の位置）に対して再帰的にチェックを続ける
            self.shift_up(num // 2)

    def delete_min(self):
        # ============================================================
        # delete_min: ヒープから最小値（根）を取り出して返すメソッド — O(log n)
        # ============================================================
        # 返り値: 最小値のデータ（Stopオブジェクト）、ヒープが空なら None
        #
        # 【手順】
        #   1. ヒープが空（size == 0）なら None を返す
        #   2. 根（インデックス1）の値を min_val に保存する
        #   3. 末尾の要素を根に移動する
        #   4. 末尾を削除し、サイズを1減らす
        #   5. shift_down() で根から下に向かってヒープ条件を回復する
        #
        # 【なぜ末尾の要素を根に移動するのか】
        #   根を直接削除すると木の構造が壊れてしまう。
        #   末尾の要素を根に持ってきてから shift_down() で正しい位置に下ろすことで、
        #   完全二分木の構造を保ったままヒープ条件を回復できる。
        #
        # 【具体例: 最小値を取り出す】
        #
        #   取り出し前:
        #          10          ← これを取り出したい（最小値）
        #         /  \
        #       20    30
        #      /  \
        #    40    50   ← 末尾はこの50
        #
        #   ステップ1: 根(10)を保存、末尾(50)を根に移動
        #          50          ← 末尾の50を根に持ってきた
        #         /  \
        #       20    30
        #      /
        #    40
        #
        #   ステップ2: shift_down() で50を正しい位置に下ろす
        #   50 の子: 左=20, 右=30 → 小さい方は 20
        #   50 > 20 → 交換!
        #          20
        #         /  \
        #       50    30
        #      /
        #    40
        #
        #   50 の子: 左=40 のみ
        #   50 > 40 → 交換!
        #          20
        #         /  \
        #       40    30
        #      /
        #    50
        #
        #   50 は葉なので終了。min_val=10 を返す。
        #
        # 【8-1.py での使われ方】
        #   delete_min() を繰り返し呼んで、小さい順にデータを取り出す。
        #   取り出したデータを enqueue() でキューに入れていく。
        #   → ヒープが空になるまで繰り返すと、キューには小さい順にデータが入る。
        # ============================================================
        if self.size == 0:
            return None  # ヒープが空のときはNoneを返す
        min_val = self.list[1]  # 根（最小値）を保存
        self.list[1] = self.list[self.size]  # 末尾の要素を根に移動
        self.list.pop()  # 末尾の要素を削除
        self.size -= 1  # サイズを1減らす
        if self.size > 0:
            self.shift_down(1)  # 根から下方向にヒープ条件を回復
        return min_val  # 保存しておいた最小値を返す

    def shift_down(self, num):
        # ============================================================
        # shift_down: ヒープの「下方向への修復」を行う再帰メソッド — O(log n)
        # ============================================================
        # 引数 num: 修復を開始するノードのインデックス
        #
        # 【何をしているか】
        #   インデックス num の要素が子より大きい場合、
        #   小さい方の子と交換して下に移動させる。
        #   これを葉（子がないノード）に到達するか、
        #   子の方が大きくなるまで再帰的に繰り返す。
        #
        # 【なぜ「小さい方の子」と交換するのか】
        #   例: 親=50, 左子=20, 右子=30 の場合
        #   もし右子(30)と交換すると:
        #       30
        #      /  \
        #    20    50
        #   → 20 < 30 なのに 20 が 30 の子 → ヒープ条件違反!
        #
        #   左子(20)と交換すれば:
        #       20
        #      /  \
        #    50    30
        #   → 20 < 50 かつ 20 < 30 → ヒープ条件OK!
        #
        # 【具体例: インデックス1の要素(経度50)を下に移動】
        #
        #   初期状態:
        #          50(idx1)
        #         /  \
        #     20(idx2)  30(idx3)
        #      /
        #   40(idx4)
        #
        #   num=1: 左子=20(idx2), 右子=30(idx3) → 小さい方は 20(idx2)
        #   50 > 20 → 交換! → shift_down(2)
        #
        #          20(idx1)
        #         /  \
        #     50(idx2)  30(idx3)
        #      /
        #   40(idx4)
        #
        #   num=2: 左子=40(idx4), 右子=なし → min_child=4
        #   50 > 40 → 交換! → shift_down(4)
        #
        #          20(idx1)
        #         /  \
        #     40(idx2)  30(idx3)
        #      /
        #   50(idx4)
        #
        #   num=4: 左子=8(存在しない, 8 > size=4) → 葉なので終了
        # ============================================================

        # 左の子（num * 2）が存在しない場合は葉なので何もしない
        if num * 2 > self.size:
            return

        # 右の子（num * 2 + 1）が存在しない場合、左の子のみが候補
        if num * 2 + 1 > self.size:
            min_child = num * 2  # 左の子しかないのでそれを選ぶ
        else:
            # 左右両方の子が存在する場合、経度が小さい方を選ぶ
            if self.list[num * 2].lng < self.list[num * 2 + 1].lng:
                min_child = num * 2  # 左の子の方が小さい
            else:
                min_child = num * 2 + 1  # 右の子の方が小さい

        # 選んだ子の経度が親より小さい場合、交換して再帰的に下に移動
        if self.list[min_child].lng < self.list[num].lng:
            # 親と子を交換（Pythonの多重代入によるスワップ）
            self.list[num], self.list[min_child] = self.list[min_child], self.list[num]
            # 交換後、さらに下（子の位置）に対して再帰的にチェックを続ける
            self.shift_down(min_child)

    def build_heap(self, lst):
        # ============================================================
        # build_heap: 既存のリストからヒープを一括構築するメソッド — O(n)
        # ============================================================
        # 引数 lst: ヒープに変換したいリスト（Stopオブジェクトのリスト）
        #
        # 【手順（ボトムアップ構築法）】
        #   1. ダミーの0を先頭に追加してリストをセットする
        #   2. 最後の非葉ノード（size // 2）から根（1）まで
        #      逆順に shift_down() を適用する
        #
        # 【なぜ size // 2 から始めるのか】
        #   - インデックス size // 2 + 1 以降は全て葉ノード（子を持たない）
        #   - 葉ノードは「子がない」ので、単体で既にヒープ条件を満たしている
        #   - だから修復する必要がない
        #   - 下のレベルから順に修復することで、効率的にヒープを構築できる
        #
        # 【具体例: [50, 20, 30, 10, 40] からヒープを構築】
        #
        #   初期状態（ただの配列をツリー形式で見る）:
        #          50(idx1)
        #         /  \
        #     20(idx2)  30(idx3)
        #      /  \
        #   10(idx4)  40(idx5)
        #
        #   size=5, size//2=2 → インデックス2から1まで shift_down
        #
        #   ステップ1: shift_down(2) — インデックス2(経度20)を修復
        #   20 の子: 左=10(idx4), 右=40(idx5) → 小さい方は 10(idx4)
        #   20 > 10 → 交換!
        #          50(idx1)
        #         /  \
        #     10(idx2)  30(idx3)
        #      /  \
        #   20(idx4)  40(idx5)
        #
        #   ステップ2: shift_down(1) — インデックス1(経度50)を修復
        #   50 の子: 左=10(idx2), 右=30(idx3) → 小さい方は 10(idx2)
        #   50 > 10 → 交換!
        #          10(idx1)
        #         /  \
        #     50(idx2)  30(idx3)
        #      /  \
        #   20(idx4)  40(idx5)
        #
        #   shift_down が再帰的に続く: 50 の子は 20(idx4), 40(idx5)
        #   50 > 20 → 交換!
        #          10(idx1)
        #         /  \
        #     20(idx2)  30(idx3)
        #      /  \
        #   50(idx4)  40(idx5)
        #
        #   最終結果: 根が10（最小値）になり、ヒープ条件が全て満たされた！
        #
        # 【計算量の比較】
        #   - 要素を1つずつ insert() する方法: O(n log n)
        #     n回のinsertでそれぞれ O(log n) かかるため
        #   - build_heap() による一括構築: O(n) — はるかに効率的
        #     大半のノードは木の下の方にあり、shift_down の距離が短いため
        #
        # 【8-1.py での使われ方】
        #   self.list（全バス停データ）を build_heap に渡して、
        #   一気にヒープを構築する。
        #   その後 delete_min() を繰り返して最小値を順に取り出す。
        # ============================================================
        self.list = [0] + lst  # ダミーの0 + 元のリストでヒープ用リストを作成
        self.size = len(lst)  # サイズを設定
        # 最後の非葉ノードから根まで、逆順に shift_down() を適用
        for i in range(self.size // 2, 0, -1):
            self.shift_down(i)

    def is_heap(self):
        # ============================================================
        # is_heap: ヒープ条件が正しく満たされているかを検証するメソッド — O(n)
        # ============================================================
        # 返り値: True（ヒープ条件を満たしている）/ False（違反がある）
        #
        # 全ての子ノードについて、「親の値 <= 子の値」が成り立つかをチェックする。
        # 1つでも「子 < 親」のノードがあれば、ヒープ条件に違反しているので False。
        # ============================================================
        for i in range(self.size, 1, -1):  # 末尾から2番目の要素まで逆順にチェック
            # 子（インデックスi）の経度が親（インデックス i // 2）の経度より小さい場合、
            # ヒープ条件（親 <= 子）に違反しているので False を返す
            if self.list[i].lng < self.list[i // 2].lng:
                return False
        return True  # 全てのノードでヒープ条件が満たされていれば True
