# ============================================================
# 第10回 課題1：グラフ上を歩く（ウォーク）
# ============================================================
# このプログラムでは、第9回で作成した Graph クラスを継承し、
# グラフ上のノードを辿る walk メソッドを持つ GraphWalk クラスを実装する。
#
# ■ プログラムの全体構成：
#   1. def_graph.py から Graph クラスを読み込む（第9回で作成）
#   2. GraphWalk クラス: Graph を継承し、walk メソッドを追加
#   3. メイン部分: kyotocitybus_line.dat を読み込み、5ステップ分歩く
#
#
# ================================================================
# ■ グラフ上のウォーク（歩道）とは？
# ================================================================
#
# ウォーク = グラフ上のノードを隣接ノードを辿りながら移動すること。
#
# 【イメージ: バス路線を辿る】
#   バス停A --[12号]--> バス停B --[15号]--> バス停C --[12号]--> バス停D
#   → 3ステップ分「歩いた」ことになる
#
# 【ウォークの特徴】
#   - 同じノードを何度でも再訪できる（ループする可能性がある）
#   - 各ステップで隣接ノードリストの先頭（neighbors[0]）を選ぶ
#   - 「探索」ではなく単純な「移動」
#
# 【ウォークとパスの違い】
#   ウォーク: 同じノードを再訪してもよい → A→B→A→B→A のようにループ可能
#   パス: 同じノードを再訪しない → 課題2の walk_without_loop が対応
#
#
# ================================================================
# ■ walk メソッドのアルゴリズム
# ================================================================
#
# 【使用するデータ構造】
#   visited: リスト — 訪問したノードIDを訪問順に格納
#     例: ['A', 'B', 'A', 'B', 'A', 'B']
#
#   parents: 辞書 — 各ノードへの到達経路を記録
#     key = 訪問先ノードID
#     value = (直前のノードID, エッジラベル) のタプル
#     例: {'A': None, 'B': ('A', '快速202号系統')}
#     ※ 開始地点は直前ノードがないので None
#
#   current: 文字列 — 現在地のノードID
#
# 【アルゴリズムの流れ】
#
#   1. visited に開始地点を追加、current を開始地点に設定
#   2. step 回繰り返す:
#      a. current の隣接ノードリストを取得
#      b. 隣接ノードがあれば、先頭の隣接ノード（neighbors[0]）へ移動
#      c. visited に追加、parents に経路情報を記録、current を更新
#      d. 隣接ノードがなければエラーメッセージを出して終了
#   3. visited を辿って経路を文字列として表示
#
# 【具体例: ED01_1914 から 5ステップ】
#
#   ステップ0: current = ED01_1914
#   ステップ1: neighbors[0] = ED01_1936 (快速202号系統) → 移動
#   ステップ2: neighbors[0] = ED01_1914 (快速202号系統) → 移動（元に戻る！）
#   ステップ3: neighbors[0] = ED01_1936 (快速202号系統) → 移動
#   ステップ4: neighbors[0] = ED01_1914 (快速202号系統) → 移動（また戻る）
#   ステップ5: neighbors[0] = ED01_1936 (快速202号系統) → 移動
#
#   結果: ED01_1914 --[快速202号系統]--> ED01_1936 --[快速202号系統]--> ED01_1914 ...
#   → neighbors[0] を無条件に選ぶため、2ノード間でループしている！
#   → これが課題2で改良する動機になる
#
#
# ================================================================
# ■ 穴埋め問題の解説
# ================================================================
#
#   空欄1: self.get_neighborhood(____) → current
#     理由: 現在地（current）の隣接ノードを取得するため
#
#   空欄2: parents[____] → neighbors[0].id
#     理由: 次の訪問先ノードID をキーとして parents 辞書に登録するため
#
#   空欄3: (____, neighbors[0].label) → current
#     理由: 直前のノード（= 現在地）の ID を parents の値に記録するため
#
#   空欄4: current = ____ → neighbors[0].id
#     理由: 現在地を次の訪問先に更新するため
# ============================================================

# 穴埋め問題用のプログラム
from def_graph import Graph  # Graphクラスの読み込み（第9回で実装したグラフの基本クラス）


# ============================================================
# GraphWalk クラス — Graph を継承してウォーク機能を追加
# ============================================================
# 【継承（Inheritance）とは】
#   class GraphWalk(Graph): と書くことで、Graph の全メソッド
#   (add_node, add_directed_edge, add_undirected_edge, get_neighborhood, ...)
#   を引き継ぎ、さらに walk メソッドを追加できる。
#   → Graph の機能を書き直す必要がない
# ============================================================
class GraphWalk(Graph):

    # ----------------------------------------------------------
    # walk: グラフ上を指定ステップ数だけ歩くメソッド
    # ----------------------------------------------------------
    # 引数 start: ウォーク開始ノードのID（文字列）
    # 引数 step: 辿る隣接ノードの数（整数）
    #
    # 訪問先は隣接ノードリストの先頭（neighbors[0]）を無条件に選ぶ。
    # そのため、同じノードを何度も再訪する可能性がある。
    # ----------------------------------------------------------
    def walk(self, start, step):
        visited = [start]  # 開始地点のノードIDを訪問済みリストに追加
        parents = {start:None}  # 直前のノードとエッジのペアを格納する辞書
        # parents の構造:
        #   key = 訪問中ノードID
        #   value = (直前のノードID, エッジラベル) のタプル、または None（開始地点）
        #   例: {'ED01_1914': None, 'ED01_1936': ('ED01_1914', '快速202号系統')}

        current = start  # 開始地点のノードIDを探索基点のノードに設定

        for i in range(step):  # step回、訪問を繰り返す
            if current not in self.node_list:  # 訪問中ノードがグラフに存在しなければ
                print('{}のノードがグラフ上に存在しません。'.format(current))
                return  # エラーメッセージを出して終了

            # 【穴埋め1】current の隣接ノードリストを取得
            # get_neighborhood は Neighbor オブジェクトのリストを返す
            # 各 Neighbor には .id（ノードID）と .label（エッジラベル）がある
            neighbors = self.get_neighborhood(current)  # 訪問中のノードの隣接ノードを取得

            if neighbors != []:  # 隣接ノードがあれば以下を実行
                # neighbors[0] = 近傍リストの先頭のNeighborオブジェクト
                # （insert(0, ...)で追加されたため、最後に追加されたエッジが先頭にある）
                visited.append(neighbors[0].id)  # 次の訪問先のIDをvisitedリストに追加

                # 【穴埋め2, 3】parents辞書に経路情報を記録
                # key: 次の訪問先ノードID（neighbors[0].id）
                # value: (現在地のID, エッジラベル) のタプル
                parents[neighbors[0].id] = (current, neighbors[0].label)  # 経路情報を記録

                # 【穴埋め4】現在地を次の訪問先に更新
                current = neighbors[0].id  # 訪問先候補を次の訪問先に決定
            else:  # 隣接ノードが無ければ（孤立ノードの場合）終了
                print('{}から{}ステップ分進むパスはありません。'.format(start, step))
                return

        # --- 訪問経路を文字列として表示 ---
        # visited リストを辿り、parents 辞書からエッジラベルを取得して
        # 「ノード --[ラベル]--> ノード --[ラベル]--> ...」形式の文字列を構築する
        path = visited[0]  # 最初に訪問したノード（開始地点）をpathに代入
        for v in visited[1:]:  # 2番目以降の訪問ノードを順に取得
            # parents[v][1] = ノードvに到達するために辿ったエッジのラベル
            # parents[v][0] = ノードvの直前のノードID（ここでは使わない）
            path = path + ' --[' + parents[v][1] +']--> ' + v  # 経路を連結
        print(path)  # 完成した経路を表示


# ============================================================
# メインの実行部分
# ============================================================
if __name__ == '__main__':  # モジュールとしてインポートされるときは実行しない

    # --- データの読み込みとグラフ構築 ---
    fi = open('kyotocitybus_line.dat', 'r', encoding = 'utf-8')
    bus_network = GraphWalk()  # GraphWalk のインスタンスを作成（Graph の全機能 + walk を持つ）
    lines = fi.readlines()
    for line in lines:
        line = line.rstrip()  # 行末の改行文字を除去
        items = line.split(' ')  # 1行を半角スペースで区切ってitemsリストに代入
        # items[0]=バス停ID1, items[1]=バス停ID2, items[2]='京都市', items[3]=路線名
        bus_network.add_undirected_edge(items[0], items[1], items[3])  # 無向エッジを追加

    # --- ウォークの実行 ---
    # ED01_1914（バス停）から 5ステップ分歩く
    bus_network.walk('ED01_1914', 5)
    fi.close()
