# ============================================================
# 第6回 課題2：選択ソート (Selection Sort)
# ============================================================
# このプログラムでは、課題1のバブルソートに加えて、
# 選択ソートによるソートも実装しています。
#
# ■ プログラムの全体構成：
#   1. DB クラス（親クラス）— データの管理（課題1と同じ）
#   2. DBwBubbleSort クラス — バブルソート（課題1と同じ）
#   3. DBwSelectSort クラス — 選択ソート（課題2で新たに追加）
#   4. 実行部分で選択ソートを使ってデータをソート
#
# ■ 選択ソートのアルゴリズム概要：
#   - リストを「ソート済み部分（左側）」と「未ソート部分（右側）」に分ける
#   - 未ソート部分から最小値を探し出す
#   - その最小値を未ソート部分の先頭と交換する
#   - これを繰り返すと、左側からソート済みの部分が伸びていく
#   - 計算量: O(n^2) — バブルソートと同じオーダーだが、
#     交換回数が最大 n-1 回で済むため、バブルソートより速いことが多い
#
# ■ バブルソートと選択ソートの比較：
#   バブルソート: 隣り合う要素を何度も交換 → 交換回数が多い
#   選択ソート:   最小値を見つけてから1回だけ交換 → 交換回数が少ない
# ============================================================

# モジュールの読み込み
from def_stop import Stop  # Stopクラスの読み込み（バス停1件分のデータを表すクラス）
import time  # 実行時間を計測するためのモジュール


# ============================================================
# DB クラス（親クラス / 基底クラス）
# ============================================================
# 課題1と同一の親クラス。
# add(), display(), count(), is_sorted() を提供する。
# sort() は子クラスでオーバーライドする前提のため pass になっている。
# ============================================================
class DB:  # DBクラスの定義
    def __init__(self):
        self.list = []  # データを格納するリスト（Stopオブジェクトが入る）

    def add(self, data):
        self.list.append(data)  # データをリストの末尾に挿入

    def display(self):
        # 全バス停データを通し番号付きで表示する
        message = '{}: {}(ID:{})のバス停の緯度経度は({},{})です。'
        j = 1
        for i in self.list:
            print(message.format(j, i.name, i.id, i.lat, i.lng))
            j += 1

    def count(self):  # データの件数を返す
        return len(self.list)

    def sort(self):  # データを昇順にソートする（子クラスでオーバーライドする）
        pass

    def is_sorted(self, descent = False):  # 降順の場合は引数にTrue
        # ソート済みかどうかを隣り合う要素の比較で検証する
        for i in range(self.count()-1):
            if not descent and (self.list[i].lng > self.list[i+1].lng or self.list[i].id == self.list[i+1].id):
                return False
            elif descent and (self.list[i].lng < self.list[i+1].lng or self.list[i].id == self.list[i+1].id):
                return False
        return True


# ============================================================
# DBwBubbleSort クラス — バブルソート（課題1と同じ）
# ============================================================
class DBwBubbleSort(DB):
    def sort(self):
        n = self.count()  # データ件数を取得
        for i in range(n-1, 0, -1):  # 右端から順に2番目の要素まで最大値を確定させていく
            for j in range(i):  # 左端から順に比較＆入換えを繰り返す
                if self.list[j].lng > self.list[j+1].lng:  # 左側の方が大きい場合
                    self.list[j], self.list[j+1] = self.list[j+1], self.list[j]  # スワップ


# ============================================================
# DBwSelectSort クラス — 選択ソート（課題2で新たに追加）
# ============================================================
# DB クラスを継承し、sort() を選択ソートで実装したクラス。
#
# 【選択ソートの動作イメージ（経度で昇順ソート）】
#
#   例: [5, 3, 8, 1, 2] を昇順にソートする場合
#
#   ステップ1 (i=0): 未ソート部分 [5,3,8,1,2] から最小値 1 を探す
#     → 1 と先頭の 5 を交換 → [1, 3, 8, 5, 2]
#     ソート済み: [1] | 未ソート: [3, 8, 5, 2]
#
#   ステップ2 (i=1): 未ソート部分 [3,8,5,2] から最小値 2 を探す
#     → 2 と 3 を交換 → [1, 2, 8, 5, 3]
#     ソート済み: [1, 2] | 未ソート: [8, 5, 3]
#
#   ステップ3 (i=2): 未ソート部分 [8,5,3] から最小値 3 を探す
#     → 3 と 8 を交換 → [1, 2, 3, 5, 8]
#     ソート済み: [1, 2, 3] | 未ソート: [5, 8]
#
#   ステップ4 (i=3): 未ソート部分 [5,8] から最小値 5 を探す
#     → 5 は既に先頭なので交換不要 → [1, 2, 3, 5, 8]
#     ソート済み: [1, 2, 3, 5] | 未ソート: [8]
#
#   → 残り1個なのでソート完了！
#
# 【ポイント】
#   - 外側のループ(i): ソート済み部分の右端を1つずつ伸ばす
#   - 内側のループ(j): 未ソート部分から最小値のインデックスを探す
#   - 交換は外側ループ1回につき最大1回だけ → バブルソートより交換回数が少ない
# ============================================================
class DBwSelectSort(DB):
    def sort(self):
        n = self.count()  # データ件数を取得

        # 外側のループ: i はソート済み部分の末尾（=未ソート部分の先頭）
        # i = 0, 1, 2, ..., n-2 まで繰り返す
        # 最後の1個は自動的に確定するので n-1 回でよい
        for i in range(n - 1):
            min_idx = i  # 未ソート部分の先頭を暫定の最小値とする

            # 内側のループ: 未ソート部分の残り(i+1 ~ n-1)を走査して最小値を探す
            for j in range(i + 1, n):  # 残りの要素と比較
                # 現在の最小値より小さい経度が見つかったら、そのインデックスを記録
                if self.list[j].lng < self.list[min_idx].lng:
                    min_idx = j  # より小さい値が見つかったらインデックスを更新

            # 最小値が見つかったら、未ソート部分の先頭(i)と交換する
            # min_idx == i の場合（=先頭が既に最小）でも交換は行われるが、
            # 同じ位置なので実質何も変わらない
            self.list[i], self.list[min_idx] = self.list[min_idx], self.list[i]


# ============================================================
# メインの実行部分
# ============================================================
if __name__ == '__main__':
    # --- データの読み込み ---
    fi = open('allkyotobus_stop.dat', 'r', encoding='utf-8')
    lines = fi.readlines()  # ファイル全体を行ごとのリストとして取得

    # ここを DBwSelectSort() にすることで課題2の選択ソートが実行されます
    # （DBwBubbleSort() に変更すれば課題1のバブルソートが実行されます）
    db = DBwSelectSort()

    # --- データの格納 ---
    for line in lines:
        line = line.rstrip()  # 行末の改行文字を除去
        items = line.split(' ')  # 1行を半角スペースで区切ってitemsリストに代入
        # items[0]=ID, items[1]=名前, items[2]=緯度, items[3]=経度, items[4:]=路線リスト
        db.add(Stop(items[0], items[1], float(items[2]), float(items[3]), items[4:]))  # Stopインスタンスを追加

    # --- ソートの実行と時間計測 ---
    start = time.time()  # ソート前の時間を記録
    db.sort()  # データのソートを実行（選択ソートが呼ばれる）
    elapsed_time = time.time() - start  # ソートにかかった時間を計算

    # --- 結果の表示 ---
    db.display()  # ソート済みの全データを表示

    if db.is_sorted():
        print("ソートが完了しました！{}は{}秒かかりました。".format(db.__class__.__name__, elapsed_time))

    fi.close()  # ファイルを閉じる
