# ============================================================
# 第6回 課題3：挿入ソート (Insertion Sort) + 3つのソートの比較
# ============================================================
# このプログラムでは、課題1のバブルソート・課題2の選択ソートに加えて、
# 挿入ソートを新たに実装し、3つのソートアルゴリズムの実行速度を比較します。
#
# ■ プログラムの全体構成：
#   1. DB クラス（親クラス）— データの管理（課題1・2と同じ）
#   2. DBwBubbleSort クラス — バブルソート（課題1と同じ）
#   3. DBwSelectSort クラス — 選択ソート（課題2と同じ）
#   4. DBwInsertSort クラス — 挿入ソート（課題3で新たに追加）
#   5. 実行部分で3つのソートを順に実行し、それぞれの実行時間を表示
#
# ■ 挿入ソートのアルゴリズム概要：
#   - リストを「ソート済み部分（左側）」と「未ソート部分（右側）」に分ける
#   - 未ソート部分の先頭要素を取り出し、ソート済み部分の正しい位置に挿入する
#   - トランプのカードを手札に挿入するイメージに近い
#   - 計算量: O(n^2) — ただし、ほぼソート済みのデータに対しては O(n) に近くなる
#
# ■ 3つのソートアルゴリズムの比較：
#   バブルソート:  隣り合う要素を交換 → 交換回数が多く最も遅いことが多い
#   選択ソート:    最小値を探して1回交換 → 交換回数は少ないが比較回数は同じ
#   挿入ソート:    正しい位置にずらして挿入 → ほぼ整列済みデータに強い
#
#   ※ いずれも最悪計算量は O(n^2) だが、実際の速度はデータの状態や
#     交換・比較のコストによって異なる
# ============================================================

# モジュールの読み込み
from def_stop import Stop  # Stopクラスの読み込み（バス停1件分のデータを表すクラス）
import time  # 実行時間を計測するためのモジュール


# ============================================================
# DB クラス（親クラス / 基底クラス）
# ============================================================
# 課題1・2と同一の親クラス。全ソートクラスの共通機能を提供する。
# ============================================================
class DB:
    def __init__(self):
        self.list = []  # Stopオブジェクトを格納するリスト

    def add(self, data):
        self.list.append(data)  # リスト末尾にデータを追加

    def display(self):
        # 全データを通し番号付きで表示
        message = '{}: {}(ID:{})のバス停の緯度経度は({},{})です。'
        j = 1
        for i in self.list:
            print(message.format(j, i.name, i.id, i.lat, i.lng))
            j += 1

    def count(self):
        return len(self.list)  # リストの要素数を返す

    def sort(self):
        pass  # 子クラスでオーバーライドする

    def is_sorted(self, descent = False):
        # ソート済みかを検証する（descent=True で降順チェック）
        for i in range(self.count()-1):
            if not descent and (self.list[i].lng > self.list[i+1].lng or self.list[i].id == self.list[i+1].id):
                return False
            elif descent and (self.list[i].lng < self.list[i+1].lng or self.list[i].id == self.list[i+1].id):
                return False
        return True


# ============================================================
# DBwBubbleSort クラス — バブルソート（課題1と同じ）
# ============================================================
# 隣り合う要素を比較・交換して、大きい値を右端に浮き上がらせていく。
# ============================================================
class DBwBubbleSort(DB):
    def sort(self):
        n = self.count()
        for i in range(n-1, 0, -1):       # 確定済み要素を右端から広げる
            for j in range(i):             # 左端から i-1 まで隣同士を比較
                if self.list[j].lng > self.list[j+1].lng:  # 左 > 右 なら交換
                    self.list[j], self.list[j+1] = self.list[j+1], self.list[j]


# ============================================================
# DBwSelectSort クラス — 選択ソート（課題2と同じ）
# ============================================================
# 未ソート部分から最小値を探し、先頭と交換する。
# ============================================================
class DBwSelectSort(DB):
    def sort(self):
        n = self.count()
        for i in range(n - 1):             # i: ソート済み部分の末尾
            min_idx = i                     # 暫定の最小値インデックス
            for j in range(i + 1, n):       # 残りから最小値を探す
                if self.list[j].lng < self.list[min_idx].lng:
                    min_idx = j             # より小さい値を発見
            self.list[i], self.list[min_idx] = self.list[min_idx], self.list[i]  # 交換


# ============================================================
# DBwInsertSort クラス — 挿入ソート（課題3で新たに追加）
# ============================================================
# DB クラスを継承し、sort() を挿入ソートで実装したクラス。
#
# 【挿入ソートの動作イメージ（経度で昇順ソート）】
#
#   例: [5, 3, 8, 1, 2] を昇順にソートする場合
#
#   初期状態: ソート済み=[5] | 未ソート=[3, 8, 1, 2]
#   （最初の1要素は「ソート済み」とみなす）
#
#   ステップ1 (i=1): key=3 を取り出す
#     ソート済み部分 [5] と比較 → 5 > 3 なので 5 を右にずらす
#     [_, 5, 8, 1, 2]  ← 5 が右にずれて空きができた
#     空いた位置に 3 を挿入 → [3, 5, 8, 1, 2]
#     ソート済み: [3, 5] | 未ソート: [8, 1, 2]
#
#   ステップ2 (i=2): key=8 を取り出す
#     ソート済み部分 [3, 5] と比較 → 5 < 8 なので移動不要
#     そのまま → [3, 5, 8, 1, 2]
#     ソート済み: [3, 5, 8] | 未ソート: [1, 2]
#
#   ステップ3 (i=3): key=1 を取り出す
#     ソート済み部分を右から比較:
#       8 > 1 → 8 を右にずらす → [3, 5, _, 8, 2]
#       5 > 1 → 5 を右にずらす → [3, _, 5, 8, 2]
#       3 > 1 → 3 を右にずらす → [_, 3, 5, 8, 2]
#     先頭に 1 を挿入 → [1, 3, 5, 8, 2]
#     ソート済み: [1, 3, 5, 8] | 未ソート: [2]
#
#   ステップ4 (i=4): key=2 を取り出す
#     ソート済み部分を右から比較:
#       8 > 2 → 8 を右にずらす → [1, 3, 5, _, 8]
#       5 > 2 → 5 を右にずらす → [1, 3, _, 5, 8]
#       3 > 2 → 3 を右にずらす → [1, _, 3, 5, 8]
#       1 < 2 → ストップ！
#     空いた位置に 2 を挿入 → [1, 2, 3, 5, 8]
#
#   → ソート完了！
#
# 【ポイント】
#   - 交換ではなく「ずらし（シフト）」と「挿入」で並べ替える
#   - while ループで挿入位置を探しながら、大きい要素を右にずらす
#   - 既にソート済みのデータなら while の条件が即 False になり、
#     ほとんど何もしないので非常に高速（最良ケースで O(n)）
# ============================================================
class DBwInsertSort(DB):
    def sort(self):
        n = self.count()

        # 外側のループ: i=1 から開始（インデックス0の要素は最初からソート済みとみなす）
        # i は「これからソート済み部分に挿入する要素」のインデックス
        for i in range(1, n):
            # key_item: 挿入したい要素を一時変数に退避する
            # この要素の正しい位置をソート済み部分の中から探す
            key_item = self.list[i]  # 挿入したい要素を一時保存

            # j: ソート済み部分の末尾（= i の1つ左）から開始
            j = i - 1

            # while ループ: ソート済み部分を右から左へ走査する
            # 条件1: j >= 0 — リストの先頭を超えないようにする（境界チェック）
            # 条件2: self.list[j].lng > key_item.lng — ソート済み部分の要素が
            #         挿入したい要素より大きい間は、右にずらし続ける
            # 両方の条件が True の間だけループが続く
            while j >= 0 and self.list[j].lng > key_item.lng:
                # self.list[j] を1つ右(j+1)にコピーして「ずらす」
                # これにより、挿入するための空きスペースが作られる
                self.list[j + 1] = self.list[j]
                j -= 1  # 1つ左の要素に移動して、さらに比較を続ける

            # while ループを抜けた時点で、j+1 が挿入すべき位置
            # （j の要素は key_item 以下か、j が -1 で先頭に到達している）
            self.list[j + 1] = key_item  # 空いた位置に要素を挿入


# ============================================================
# メインの実行部分 — 3つのアルゴリズムを比較
# ============================================================
# 3つのソートクラスそれぞれに同じデータを入れてソートし、
# 各アルゴリズムの実行時間を比較表示する。
# ============================================================
if __name__ == '__main__':
    # --- データの読み込み ---
    fi = open('allkyotobus_stop.dat', 'r', encoding='utf-8')
    lines = fi.readlines()  # ファイル全体を行ごとのリストとして取得
    fi.close()  # 先にファイルを閉じる（この後 lines リストだけを使う）

    # 3つのソートクラスのインスタンスをリストにまとめる
    # こうすることで、for ループで順番に全てのソートを実行できる
    dbs = [DBwBubbleSort(), DBwSelectSort(), DBwInsertSort()]

    # --- それぞれのソートを実行してタイムを計測 ---
    for db in dbs:
        # 各 db に同じデータを追加する
        # ※ 毎回新しいインスタンスにデータを入れ直すので、
        #   前のソート結果が次のソートに影響しない
        for line in lines:
            line = line.rstrip()  # 行末の改行を除去
            items = line.split(' ')  # スペースで分割
            # Stop オブジェクトを生成して db に追加
            db.add(Stop(items[0], items[1], float(items[2]), float(items[3]), items[4:]))

        start = time.time()  # ソート前の時間を記録
        db.sort()            # ソートを実行（各クラスのsort()が呼ばれる）
        elapsed_time = time.time() - start  # 実行時間を計算

        # 今回はデータの全表示(db.display())は行わず、結果だけを出力する
        # 全データを表示すると出力が膨大になるため、時間だけを比較する
        if db.is_sorted():
            # __class__.__name__ で「DBwBubbleSort」「DBwSelectSort」「DBwInsertSort」
            # のようにクラス名が文字列として取得できる
            print("ソートが完了しました！{}は{}秒かかりました。".format(db.__class__.__name__, elapsed_time))
