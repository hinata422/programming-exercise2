# ============================================================
# 第6回 課題1：バブルソート (Bubble Sort)
# ============================================================
# このプログラムでは、京都のバス停データを「経度(lng)」の昇順に
# バブルソートで並べ替えます。
#
# ■ プログラムの全体構成：
#   1. def_stop.py から Stop クラスを読み込む
#   2. DB クラス（親クラス）でデータの格納・表示・件数取得・ソート済み判定を行う
#   3. DBwBubbleSort クラス（子クラス）で sort() をバブルソートとしてオーバーライド
#   4. allkyotobus_stop.dat からバス停データを読み込み、ソートして表示する
#
# ■ バブルソートのアルゴリズム概要：
#   - 隣り合う2つの要素を比較し、順序が逆なら交換する
#   - これをリストの端から端まで繰り返す（1パスと呼ぶ）
#   - 1パスごとに最大値が右端に「浮き上がる（バブル）」ように確定する
#   - パスを繰り返すことで全体がソートされる
#   - 計算量: O(n^2) — データ数が増えると急激に遅くなる
#
# ■ 継承（クラスの親子関係）について：
#   - DB クラスが「親クラス（基底クラス）」
#   - DBwBubbleSort が「子クラス（派生クラス）」
#   - 子クラスは親クラスの全メソッド（add, display, count, is_sorted）を引き継ぐ
#   - 子クラスで sort() を再定義（オーバーライド）して、独自のソート処理を実装する
# ============================================================

from def_stop import Stop  # Stopクラスの読み込み（バス停1件分のデータを表すクラス）
import time  # 実行時間を計測するためのモジュール


# ============================================================
# DB クラス（親クラス / 基底クラス）
# ============================================================
# バス停データを管理するための基本クラス。
# データの追加・表示・件数取得・ソート済み判定の機能を持つ。
# sort() メソッドは pass（何もしない）になっており、
# 子クラスで具体的なソートアルゴリズムをオーバーライドして使う。
# ============================================================
class DB:  # DBクラスの定義
    def __init__(self):
        # インスタンス変数 self.list を空リストで初期化
        # ここに Stop オブジェクトが順番に格納されていく
        self.list = []  # データを格納するリスト

    def add(self, data):
        # 引数 data（Stopオブジェクト）をリストの末尾に追加する
        # append() はリストの末尾に要素を1つ追加するメソッド
        self.list.append(data)  # データをリストの末尾に挿入

    def display(self):
        # リスト内の全データを番号付きで表示するメソッド
        # format() を使って、通し番号・名前・ID・緯度・経度を埋め込む
        message = '{}: {}(ID:{})のバス停の緯度経度は({},{})です。'
        j = 1  # 表示用の通し番号（1から始まる）
        for i in self.list:  # self.list の各 Stop オブジェクトに対してループ
            # i.name → バス停名, i.id → ID, i.lat → 緯度, i.lng → 経度
            print(message.format(j, i.name, i.id, i.lat, i.lng))
            j += 1  # 通し番号をインクリメント

    def count(self):  # データの件数を返す
        # len() でリストの要素数を取得して返す
        return len(self.list)

    def sort(self):  # データを昇順にソートする
        # 親クラスでは何もしない（子クラスでオーバーライドする前提）
        # pass は「何もしない」を意味するPythonの文
        pass

    def is_sorted(self, descent = False):  # 降順の場合は引数にTrue
        # ソートが正しく完了しているかを検証するメソッド
        # descent=False（デフォルト）: 昇順チェック
        # descent=True: 降順チェック
        for i in range(self.count()-1):  # 隣り合う要素を順番に比較
            # 昇順チェック: 前の要素の経度が次の要素より大きい → ソートされていない
            # また、同じIDの要素が隣り合っている場合もFalse（重複データの検出）
            if not descent and (self.list[i].lng > self.list[i+1].lng or self.list[i].id == self.list[i+1].id):
                return False
            # 降順チェック: 前の要素の経度が次の要素より小さい → ソートされていない
            elif descent and (self.list[i].lng < self.list[i+1].lng or self.list[i].id == self.list[i+1].id):
                return False
        return True  # 全ての隣り合う要素が正しい順序ならTrue


# ============================================================
# DBwBubbleSort クラス（子クラス）— バブルソート
# ============================================================
# DB クラスを継承し、sort() をバブルソートで実装したクラス。
#
# 【バブルソートの動作イメージ（経度で昇順ソート）】
#
#   例: [5, 3, 8, 1, 2] を昇順にソートする場合
#
#   パス1 (i=4): 左端から右へ比較・交換を繰り返す
#     [5,3,8,1,2] → [3,5,8,1,2] → [3,5,8,1,2] → [3,5,1,8,2] → [3,5,1,2,8]
#     → 最大値 8 が右端に確定
#
#   パス2 (i=3): 右端の8は確定済みなので、残りの部分で同様に
#     [3,5,1,2,8] → [3,5,1,2,8] → [3,1,5,2,8] → [3,1,2,5,8]
#     → 5 が確定
#
#   パス3 (i=2):
#     [3,1,2,5,8] → [1,3,2,5,8] → [1,2,3,5,8]
#     → 3 が確定
#
#   パス4 (i=1):
#     [1,2,3,5,8] → 交換なし
#     → ソート完了！
# ============================================================
class DBwBubbleSort(DB):  # バブルソート用のクラス（DBを継承）
    def sort(self):
        n = self.count()  # データ件数を取得（例: 5000件なら n=5000）

        # 外側のループ: i は「まだソートされていない範囲の右端」を示す
        # range(n-1, 0, -1) → n-1, n-2, ..., 2, 1 と減少していく
        # パスごとに確定する要素が増え、比較範囲が狭まっていく
        for i in range(n-1, 0, -1):  # 右端から順に2番目の要素まで最大値を確定させていく

            # 内側のループ: j は「今比較している左側の要素のインデックス」
            # range(i) → 0, 1, ..., i-1 まで
            # self.list[j] と self.list[j+1] を比較する
            for j in range(i):  # 左端から順に比較＆入換えを繰り返す

                # 隣り合う2つの要素の経度(lng)を比較
                # 左側の経度が右側より大きければ、順序が逆なので交換する
                if self.list[j].lng > self.list[j+1].lng:  # 左側の方が大きい場合
                    # Pythonの多重代入を使ったスワップ（値の入れ替え）
                    # 一時変数を使わずに、2つの変数の値を同時に交換できる
                    # 例: a, b = b, a で a と b の値が入れ替わる
                    self.list[j], self.list[j+1] = self.list[j+1], self.list[j]  # スワップ


# ============================================================
# メインの実行部分
# ============================================================
# if __name__ == '__main__': は、このファイルが直接実行された場合のみ
# 以下のコードを実行するという意味。
# 他のファイルから import された場合は実行されない。
# ============================================================
if __name__ == '__main__':  # モジュールとしてインポートされるときは実行しない

    # --- データの読み込み ---
    # allkyotobus_stop.dat ファイルを読み込みモードで開く
    # encoding='utf-8' で日本語の文字化けを防ぐ
    fi = open('allkyotobus_stop.dat', 'r', encoding='utf-8')

    # readlines() でファイル全体を行ごとのリストとして読み込む
    # 各要素は "ID 名前 緯度 経度 路線1 路線2 ..." という形式の文字列
    lines = fi.readlines()

    # バブルソート用のDBインスタンスを作成
    db = DBwBubbleSort()

    # --- データの格納 ---
    for line in lines:
        line = line.rstrip()  # 行末の改行文字(\n)を除去
        items = line.split(' ')  # 1行を半角スペースで区切ってitemsリストに代入
        # items[0] → ID, items[1] → 名前, items[2] → 緯度, items[3] → 経度
        # items[4:] → 5番目以降の全要素（路線名のリスト）
        # float() で文字列を小数に変換（緯度・経度は数値として扱うため）
        db.add(Stop(items[0], items[1], float(items[2]), float(items[3]), items[4:]))  # Stopインスタンスをdbに追加

    # --- ソートの実行と時間計測 ---
    start = time.time()  # ソート前の時間を記録（time()は現在時刻を返すメソッド）
    db.sort()  # データのソートを実行（バブルソートが呼ばれる）
    elapsed_time = time.time() - start  # ソート後の時間からソート前の時間を引き、実行時間を計測

    # --- 結果の表示 ---
    db.display()  # ソート済みの全データを表示

    # is_sorted() でソートが正しく完了したか検証し、結果を表示
    if db.is_sorted():
        # __class__ はインスタンスが属するクラスオブジェクトを返す
        # __name__ はそのクラスの名前を文字列で返す（例: "DBwBubbleSort"）
        print("ソートが完了しました！{}は{}秒かかりました。".format(db.__class__.__name__, elapsed_time))  # __class__はインスタンスのクラスを返し、__name__はそのクラスの名前を返す

    fi.close()  # ファイルを閉じる（リソースの解放）
