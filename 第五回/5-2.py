# ============================================================
# 第5回 課題2：再帰的線形探索 (Recursive Linear Search)
# ============================================================
# このプログラムでは、京都のバス停データの中から指定されたIDのバス停を
# 「再帰的線形探索」によって検索します。
#
# ■ プログラムの全体構成：
#   1. def_stop.py から Stop クラスを読み込む
#   2. DB クラス（親クラス）でデータの格納を行う
#   3. DBwRecursiveSearch クラス（子クラス）で再帰的な線形探索を実装
#   4. allkyotobus_stop.dat からバス停データを読み込み、IDで検索する
#
# ■ 再帰的線形探索のアルゴリズム概要：
#   - リストの先頭要素から順に、探しているIDと一致するかを確認する
#   - 一致すればその要素を返す（基底ケース：発見）
#   - 一致しなければ、先頭を除いた残りのリストで同じ処理を再帰的に行う
#   - リストが空になっても見つからなければ None を返す（基底ケース：未発見）
#   - 計算量: O(n) — データ数 n に比例して探索時間が増加する
#     最悪の場合、全要素を1回ずつ確認するため n 回の比較が必要
#
# ■ 課題1（forループによる線形探索）との違い：
#   - 課題1: for ループでリストを先頭から順に走査する（反復的アプローチ）
#   - 課題2: 自分自身を呼び出す再帰で探索する（再帰的アプローチ）
#   - どちらも計算量は O(n) で同じだが、実装の考え方が異なる
#   - 再帰版はスライス(search_list[1:])で毎回新しいリストを生成するため、
#     メモリ使用量が多く、また再帰の深さに制限がある点に注意
#
# ■ 再帰の仕組み：
#   再帰とは「関数が自分自身を呼び出す」手法のこと。
#   必ず以下の2つの要素が必要：
#     (1) 基底ケース（再帰を終了する条件）
#         → リストが空になった or 目的の要素を見つけた
#     (2) 再帰ステップ（問題を小さくして自分自身を呼び出す）
#         → 先頭要素を除いた残りのリストで再度 search() を呼ぶ
#   基底ケースがないと無限再帰に陥り、スタックオーバーフローが発生する
#
# ■ 動作イメージ（IDが 'C' のデータを探す場合）：
#
#   リスト: [A, B, C, D, E]
#
#   呼び出し1: search('C', [A, B, C, D, E])
#     → 先頭は A → 一致しない → search('C', [B, C, D, E]) を呼ぶ
#
#   呼び出し2: search('C', [B, C, D, E])
#     → 先頭は B → 一致しない → search('C', [C, D, E]) を呼ぶ
#
#   呼び出し3: search('C', [C, D, E])
#     → 先頭は C → 一致した！ → C の名前を返す（再帰終了）
#
#   ※ 見つからない場合は、リストが [] になるまで再帰が続き、
#     最終的に None が返される
# ============================================================

from def_stop import Stop  # Stopクラスの読み込み（バス停1件分のデータを表すクラス）
import time  # 実行時間を計測するためのモジュール
import sys  # 再帰呼び出し回数の上限を変更するためのモジュール

# Pythonのデフォルトの再帰上限（1000回）を超えてエラーになるのを防ぐため拡張
# バス停データは数千件あるため、最悪ケースではそれだけ再帰が深くなる
sys.setrecursionlimit(10000)


# ============================================================
# DB クラス（親クラス / 基底クラス）
# ============================================================
# バス停データを管理するための基本クラス。
# データの追加機能のみを持つシンプルな構成。
# 子クラスで search() などの探索メソッドを追加して使う。
# ============================================================
class DB:  # DBクラスの定義
    def __init__(self):
        # インスタンス変数 self.list を空リストで初期化
        # ここに Stop オブジェクトが順番に格納されていく
        self.list = []  # データを格納するリスト

    def add(self, data):
        # 引数 data（Stopオブジェクト）をリストの末尾に追加する
        # append() はリストの末尾に要素を1つ追加するメソッド
        self.list.append(data)  # データをリストの末尾に挿入



# ============================================================
# DBwRecursiveSearch クラス（子クラス）— 再帰的線形探索
# ============================================================
# DB クラスを継承し、search() を再帰的な線形探索で実装したクラス。
# 親クラスの add() メソッドをそのまま引き継ぎ、
# 新たに search() メソッドを追加している。
#
# 【search_list[1:] スライスの仕組み】
#   search_list[1:] は「リストの2番目以降の全要素」を新しいリストとして返す。
#   例: [A, B, C, D] → [1:] → [B, C, D]
#        [B, C, D]   → [1:] → [C, D]
#        [C, D]      → [1:] → [D]
#        [D]          → [1:] → []  ← 空リストになったら基底ケース1
#   このように再帰のたびにリストが1つずつ短くなり、必ず終了条件に到達する。
# ============================================================
class DBwRecursiveSearch(DB):  # バブルソート用のDBクラスを継承した再帰探索クラス
    def search(self, query, search_list=None):
        # 初回呼び出し時は search_list が None なので、
        # インスタンスのリスト(self.list)全体を検索対象として設定する
        # 2回目以降の再帰呼び出しでは、呼び出し側から渡されたリストを使う
        if search_list is None:
            search_list = self.list

        # 基底ケース1: 検索対象のリストが空（最後まで探して見つからなかった場合）
        # not search_list は、search_list が空リスト [] のとき True になる
        # → 全要素を調べても一致するものがなかったので None を返す
        if not search_list:
            return None

        # 基底ケース2: リストの先頭のIDがクエリと一致した場合
        # search_list[0] はリストの先頭要素（Stopオブジェクト）
        # .id でそのバス停のIDを取得し、探しているqueryと比較する
        # → 一致すれば、そのバス停の名前(.name)を返して再帰を終了する
        if search_list[0].id == query:
            return search_list[0].name

        # 再帰ステップ: 一致しなければ、2番目以降のデータ列(スライス)を渡して再帰呼び出し
        # search_list[1:] で先頭要素を除いた新しいリストを生成し、
        # そのリストを対象に再度 search() を呼び出す
        # → 問題のサイズが1つ小さくなり、いずれ基底ケースに到達する
        return self.search(query, search_list[1:])
# --- ここまで ---


# ============================================================
# メインの実行部分
# ============================================================
# ファイルからバス停データを読み込み、指定したIDで検索を行い、
# 検索結果と実行時間を表示する。
# ============================================================

# --- データの読み込み ---
# allkyotobus_stop.dat ファイルを読み込みモードで開く
# encoding='utf-8' で日本語の文字化けを防ぐ
fi = open('allkyotobus_stop.dat', 'r', encoding='utf-8')

# readlines() でファイル全体を行ごとのリストとして読み込む
# 各要素は "ID 名前 緯度 経度 路線1 路線2 ..." という形式の文字列
lines = fi.readlines()

# 検索したいバス停IDのリスト
# 'ED01_5000' は存在しないIDなので、検索結果は None になるはず
queries = ['ED01_1653', 'ED01_4089', 'ED01_5000']

# 再帰探索クラスのインスタンスを作成
db = DBwRecursiveSearch()  # 作成した再帰探索クラスのインスタンス化

# --- データの格納 ---
for line in lines:
    line = line.rstrip()  # 行末の改行文字(\n)を除去
    items = line.split(' ')  # 1行を半角スペースで区切ってitemsリストに代入
    # items[0] → ID, items[1] → 名前, items[2] → 緯度, items[3] → 経度
    # items[4:] → 5番目以降の全要素（路線名のリスト）
    # float() で文字列を小数に変換（緯度・経度は数値として扱うため）
    db.add(Stop(items[0], items[1], float(items[2]), float(items[3]), items[4:]))

# --- 探索の実行と時間計測 ---
for query in queries:
    start = time.perf_counter()  # 探索前の時間を記録（perf_counter()は高精度な時間計測用）
    result = db.search(query)  # 再帰的線形探索を実行（queryに一致するバス停名を返す）
    elapsed_time = time.perf_counter() - start  # 探索後の時間から探索前の時間を引き、実行時間を計測
    # __class__.__name__ でクラス名（"DBwRecursiveSearch"）を文字列として取得
    print("ID:{}は{}です。{}は{}秒かかりました。".format(query, result, db.__class__.__name__, elapsed_time))

fi.close()  # ファイルを閉じる（リソースの解放）
