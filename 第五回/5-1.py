# ============================================================
# 第5回 課題1：線形探索 (Linear Search)
# ============================================================
# このプログラムでは、京都のバス停データから指定したIDのバス停を
# 線形探索（リニアサーチ）で検索し、その名前を表示します。
#
# ■ プログラムの全体構成：
#   1. def_stop.py から Stop クラスを読み込む
#   2. DB クラス（親クラス）でデータの格納機能を持つ
#   3. DBwLinerSearch クラス（子クラス）で search() を線形探索として実装
#   4. allkyotobus_stop.dat からバス停データを読み込み、探索して結果を表示する
#
# ■ 線形探索のアルゴリズム概要：
#   - リストの先頭から末尾まで、1つずつ順番に探しているデータと比較する
#   - 一致するデータが見つかれば、その時点で結果を返して探索を終了する
#   - 最後まで見つからなければ None を返す（データが存在しない）
#   - 最も単純な探索アルゴリズムで、実装が容易
#   - 計算量: O(n) — データ数 n に比例して探索時間が増加する
#     - 最良の場合: 先頭で見つかる → O(1)
#     - 最悪の場合: 末尾で見つかる or 見つからない → O(n)
#     - 平均: n/2 回の比較 → O(n)
#
# ■ 線形探索の動作イメージ：
#   例: リスト [A, B, C, D, E] から "D" を探す場合
#
#   ステップ1: A と比較 → 一致しない → 次へ
#   ステップ2: B と比較 → 一致しない → 次へ
#   ステップ3: C と比較 → 一致しない → 次へ
#   ステップ4: D と比較 → 一致！ → D の情報を返す
#
#   例: リスト [A, B, C, D, E] から "Z" を探す場合
#
#   ステップ1〜5: A, B, C, D, E と順に比較 → 全て一致しない
#   → リスト末尾まで到達 → None を返す（見つからなかった）
#
# ■ 継承（クラスの親子関係）について：
#   - DB クラスが「親クラス（基底クラス）」
#   - DBwLinerSearch が「子クラス（派生クラス）」
#   - 子クラスは親クラスの全メソッド（add）を引き継ぐ
#   - 子クラスで search() を新たに定義して、探索機能を追加する
# ============================================================

from def_stop import Stop  # Stopクラスの読み込み（バス停1件分のデータを表すクラス）
import time  # 実行時間を計測するためのモジュール
import sys  # 再帰呼び出し回数の上限を変更するためのモジュール
sys.setrecursionlimit(10000)  # 再帰呼び出し回数の上限を10000回に変更（デフォルトは1000回）


# ============================================================
# DB クラス（親クラス / 基底クラス）
# ============================================================
# バス停データを管理するための基本クラス。
# データの追加機能のみを持つシンプルな構成。
# 子クラスで search() などの具体的な操作メソッドを追加して使う。
# ============================================================
class DB:  # DBクラスの定義
    def __init__(self):
        # インスタンス変数 self.list を空リストで初期化
        # ここに Stop オブジェクトが順番に格納されていく
        self.list = []  # データを格納するリスト

    def add(self, data):
        # 引数 data（Stopオブジェクト）をリストの末尾に追加する
        # append() はリストの末尾に要素を1つ追加するメソッド
        self.list.append(data)  # データをリストの末尾に挿入


# ============================================================
# DBwLinerSearch クラス（子クラス）— 線形探索
# ============================================================
# DB クラスを継承し、search() を線形探索で実装したクラス。
# 親クラスの add() メソッドはそのまま使い、
# 新たに search() メソッドを追加している。
#
# 【線形探索の動作イメージ】
#
#   self.list = [Stop0, Stop1, Stop2, ..., StopN]
#   query = "ED01_1653"
#
#   Stop0.id == "ED01_1653" ? → No  → 次へ
#   Stop1.id == "ED01_1653" ? → No  → 次へ
#   Stop2.id == "ED01_1653" ? → Yes → Stop2.name を返す（探索終了）
#
#   もし最後まで一致しなければ → None を返す
# ============================================================
class DBwLinerSearch(DB):  # 線形探索用のクラス（DBを継承）
    def search(self, query):
        # for文でリストの先頭から末尾まで1つずつ順番にチェックする
        # item には各 Stop オブジェクトが順番に代入される
        for item in self.list:  # ループでリストの先頭からチェック
            # 現在の要素のIDと探索対象のqueryを比較
            # 一致すればそのバス停の名前(name)を返して探索終了
            if item.id == query:
                return item.name
        # forループを最後まで回りきった場合、一致するデータがなかったことを意味する
        # None を返すことで「見つからなかった」ことを呼び出し元に伝える
        return None  # ループを抜ければ、探しているデータはなし


# ============================================================
# メインの実行部分
# ============================================================
# ファイルからバス停データを読み込み、指定したIDで線形探索を行い、
# 探索結果と所要時間を表示する。
# ============================================================

# --- データの読み込み ---
# allkyotobus_stop.dat ファイルを読み込みモードで開く
# encoding='utf-8' で日本語の文字化けを防ぐ
fi = open('allkyotobus_stop.dat', 'r', encoding='utf-8')

# readlines() でファイル全体を行ごとのリストとして読み込む
# 各要素は "ID 名前 緯度 経度 路線1 路線2 ..." という形式の文字列
lines = fi.readlines()

# 探索対象のIDを3つ指定（存在するID2つ + 存在しないID1つ）
# ED01_5000 は存在しないIDなので、探索結果は None になるはず
queries = ['ED01_1653', 'ED01_4089', 'ED01_5000']

# 線形探索用のDBインスタンスを作成
db = DBwLinerSearch()

# --- データの格納 ---
for line in lines:
    line = line.rstrip()  # 行末の改行文字(\n)を除去
    items = line.split(' ')  # 1行を半角スペースで区切ってitemsリストに代入
    # items[0] → ID, items[1] → 名前, items[2] → 緯度, items[3] → 経度
    # items[4:] → 5番目以降の全要素（路線名のリスト）
    # float() で文字列を小数に変換（緯度・経度は数値として扱うため）
    db.add(Stop(items[0], items[1], float(items[2]), float(items[3]), items[4:]))  # StopオブジェクトをDBwLinerSearchのインスタンスに追加

# --- 探索の実行と時間計測 ---
for query in queries:
    # time.perf_counter() は高精度な時間計測用の関数
    # time.time() より精度が高く、短い処理の計測に適している
    start = time.perf_counter()  # 探索前の時間を記録
    result = db.search(query)  # 線形探索を実行し、結果（バス停名 or None）を受け取る
    elapsed_time =  time.perf_counter() - start  # 探索後の時間から探索前の時間を引き、実行時間を計測
    # __class__ はインスタンスが属するクラスオブジェクトを返す
    # __name__ はそのクラスの名前を文字列で返す（例: "DBwLinerSearch"）
    print("ID:{}は{}です。{}は{}秒かかりました。".format(query, result, db.__class__.__name__, elapsed_time))  # 「オブジェクト名.__class__.__name__」でオブジェクトのクラス名を取得

fi.close()  # ファイルを閉じる（リソースの解放）
