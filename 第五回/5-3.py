# ============================================================
# 第5回 課題3：3つの探索アルゴリズムの比較
# （線形探索・再帰的線形探索・二分探索）
# ============================================================
# このプログラムでは、京都のバス停データから指定したIDのバス停を
# 3つの異なる探索アルゴリズムで検索し、実行時間を比較します。
#
# ■ プログラムの全体構成：
#   1. def_stop.py から Stop クラスを読み込む
#   2. DB クラス（親クラス）でデータの格納機能を提供する
#   3. 3つの子クラスがそれぞれ異なる探索アルゴリズムで search() を実装する
#      - DBwLinerSearch     : 線形探索（リニアサーチ）
#      - DBwRecursiveSearch : 再帰的線形探索（リカーシブサーチ）
#      - DBwBinarySearch    : 二分探索（バイナリサーチ）
#   4. allkyotobus_stop.dat からバス停データを読み込み、
#      各アルゴリズムで同じクエリを検索して実行時間を比較する
#
# ■ 3つの探索アルゴリズムの計算量比較：
#   ┌──────────────────────┬──────────────┬───────────────────────────┐
#   │ アルゴリズム         │ 計算量       │ 特徴                      │
#   ├──────────────────────┼──────────────┼───────────────────────────┤
#   │ 線形探索             │ O(n)         │ 先頭から順に探す（単純）   │
#   │ 再帰的線形探索       │ O(n)         │ 再帰で先頭から探す         │
#   │ 二分探索             │ O(log n)     │ ソート済みデータが前提     │
#   └──────────────────────┴──────────────┴───────────────────────────┘
#   ※ n はデータの件数。例えば n=5000 の場合:
#     - 線形探索・再帰的線形探索: 最大5000回の比較が必要
#     - 二分探索: 最大 log2(5000) ≒ 13回 の比較で済む
#
# ■ 動作イメージ（二分探索の例）：
#   データ: [1001, 1200, 1500, 2000, 2500, 3000, 3500, 4000, 4089]
#   クエリ: 4089 を探す場合
#
#   ステップ1: 真ん中 = 2500 → 4089 > 2500 → 右半分 [3000, 3500, 4000, 4089] を探索
#   ステップ2: 真ん中 = 3500 → 4089 > 3500 → 右半分 [4000, 4089] を探索
#   ステップ3: 真ん中 = 4000 → 4089 > 4000 → 右半分 [4089] を探索
#   ステップ4: 真ん中 = 4089 → 一致！ → 発見
#   → たった4回の比較で見つかる（線形探索なら9回必要）
#
# ■ 継承（クラスの親子関係）について：
#   - DB クラスが「親クラス（基底クラス）」
#   - DBwLinerSearch, DBwRecursiveSearch, DBwBinarySearch が「子クラス（派生クラス）」
#   - 子クラスは親クラスの全メソッド（__init__, add）を引き継ぐ
#   - 子クラスで search() を定義して、独自の探索処理を実装する
# ============================================================

from def_stop import Stop  # Stopクラスの読み込み（バス停1件分のデータを表すクラス）
import time  # 実行時間を計測するためのモジュール
import sys  # Pythonのシステム設定を変更するためのモジュール
sys.setrecursionlimit(10000)  # 再帰の上限を10000回に拡大（デフォルトは約1000回）
# ※ 再帰的線形探索でデータ件数分だけ再帰するため、上限を増やす必要がある


# ============================================================
# DB クラス（親クラス / 基底クラス）
# ============================================================
# バス停データを管理するための基本クラス。
# データの追加機能（add）を持ち、探索機能は子クラスで実装する。
# ============================================================
class DB:  # DBクラスの定義
    def __init__(self):
        # インスタンス変数 self.list を空リストで初期化
        # ここに Stop オブジェクトが順番に格納されていく
        self.list = []  # データを格納するリスト

    def add(self, data):
        # 引数 data（Stopオブジェクト）をリストの末尾に追加する
        # append() はリストの末尾に要素を1つ追加するメソッド
        self.list.append(data)  # データをリストの末尾に挿入


# ============================================================
# DBwLinerSearch クラス（子クラス）— 線形探索（リニアサーチ）
# ============================================================
# DB クラスを継承し、search() を線形探索で実装したクラス。
#
# 【線形探索のアルゴリズム概要】
#   - リストの先頭から末尾まで、1つずつ順番にデータを確認する
#   - 目的のデータが見つかったら即座にその名前を返す
#   - リスト全体を調べても見つからなければ None を返す
#   - 計算量: O(n) — データ数に比例して探索時間が増える
#
# 【動作イメージ】
#   データ: [ID_1001, ID_1200, ID_1500, ID_2000, ID_4089]
#   クエリ: ID_4089 を探す場合
#
#   ID_1001 → 不一致 → 次へ
#   ID_1200 → 不一致 → 次へ
#   ID_1500 → 不一致 → 次へ
#   ID_2000 → 不一致 → 次へ
#   ID_4089 → 一致！ → "バス停名" を返す
#   → 5回の比較が必要（最悪の場合、全件を調べる）
# ============================================================
# 線形探索
class DBwLinerSearch(DB): # 線形探索用のクラス→前から尋ねる
    def search(self, query):
        for item in self.list: # ループでリストの先頭からチェック
            if item.id == query:  # 現在の要素のIDがクエリと一致するか判定
                return item.name  # 一致したら、そのバス停の名前を返す（探索終了）
        return None # ループを抜ければ、探しているデータはなし


# ============================================================
# DBwRecursiveSearch クラス（子クラス）— 再帰的線形探索
# ============================================================
# DB クラスを継承し、search() を再帰呼び出しによる線形探索で実装したクラス。
#
# 【再帰的線形探索のアルゴリズム概要】
#   - 先頭の要素を確認し、一致しなければ残りのリストで再帰的に探索する
#   - search() が外部から呼ばれる窓口、search_impl() が実際の再帰処理
#   - 2メソッド構成にする理由:
#     search() → ユーザが呼ぶ窓口（self.list を渡すだけ）
#     search_impl() → 再帰のたびにリストを縮小しながら呼ばれる実処理
#   - 計算量: O(n) — 線形探索と同じ（再帰にしても探索回数は変わらない）
#
# 【動作イメージ】
#   search_impl([ID_1001, ID_1200, ID_1500], query=ID_1500)
#     → list[0]=ID_1001 → 不一致 → search_impl([ID_1200, ID_1500], query=ID_1500)
#       → list[0]=ID_1200 → 不一致 → search_impl([ID_1500], query=ID_1500)
#         → list[0]=ID_1500 → 一致！ → "バス停名" を返す
#   ※ list[1:] で先頭を除いた新しいリストを作り、再帰的に渡していく
# ============================================================
class DBwRecursiveSearch(DB):  # 再帰的線形探索用のクラス（DBを継承）
    def search(self, query):
        # search_impl() に self.list 全体と検索クエリを渡して探索を開始する
        # search() はユーザが呼ぶ窓口メソッド
        result = self.search_impl(self.list, query)  # 再帰探索の開始
        return result  # 探索結果を返す（見つかればバス停名、なければNone）

    def search_impl(self,list, query):
        # 【ベースケース1】リストが空なら探索失敗 → None を返す
        if list ==[] :
            return None
        # 【ベースケース2】先頭の要素のIDがクエリと一致 → その名前を返す
        if list[0].id == query:
            return list[0].name
        else:
            # 【再帰ケース】先頭を除いた残りのリスト list[1:] で再帰的に探索
            return self.search_impl(list[1:], query)
        # ここで1以降のリストが作成される。
        # ※ list[1:] はスライスで、インデックス1以降の要素からなる新しいリストを生成する
        # ※ 再帰のたびにリストが1つずつ短くなり、最終的に空リスト [] になる


# ============================================================
# DBwBinarySearch クラス（子クラス）— 二分探索（バイナリサーチ）
# ============================================================
# DB クラスを継承し、search() を二分探索で実装したクラス。
#
# 【二分探索のアルゴリズム概要】
#   - ソート済みデータが大前提（IDの数値部分が昇順に並んでいる必要がある）
#   - リストの真ん中の要素を調べ、クエリと比較する
#   - クエリが真ん中より大きければ右半分、小さければ左半分を再帰的に探索
#   - 探索範囲が毎回半分に狭まるため、非常に高速
#   - 計算量: O(log n) — データ数が2倍になっても比較回数は1回増えるだけ
#
# 【重要なポイント】
#   - m = len(list) // 2 : リストの中央インデックスを整数除算で求める
#     例: len=5 → m=2, len=4 → m=2, len=1 → m=0
#   - int(list[m].id[5:]) : IDは "ED01_1653" のような形式。
#     id[5:] で6文字目以降の数値部分 "1653" を取り出し、int() で整数に変換する。
#     これにより、文字列ではなく数値として大小比較ができる。
#     ※ 文字列比較だと "9" > "10" になってしまうため、数値に変換する必要がある
#   - list[m+1:] : 中央より右側（大きい値の範囲）を再帰探索
#   - list[0:m]  : 中央より左側（小さい値の範囲）を再帰探索
#
# 【動作イメージ】
#   データ: [ED01_1001, ED01_1200, ED01_1500, ED01_2000, ED01_4089]
#   クエリ: ED01_4089 を探す場合
#
#   ステップ1: m=2, list[2]=ED01_1500
#     1500 < 4089 → 右半分 [ED01_2000, ED01_4089] を探索
#   ステップ2: m=1, list[1]=ED01_4089
#     4089 == 4089 → 一致！ → バス停名を返す
#   → たった2回の比較で発見（線形探索なら5回必要）
# ============================================================
# 再帰線形探索
# 二分探索
# 参照しているサイトがちっちゃいものから多きいものが大前提
class DBwBinarySearch(DB):  # 二分探索用のクラス（DBを継承）
    def search(self, query):
        # search_impl() に self.list 全体と検索クエリを渡して探索を開始する
        result = self.search_impl(self.list, query)  # 二分探索の開始
        return result  # 探索結果を返す（見つかればバス停名、なければNone）

    def search_impl(self, list, query):
        m=len(list)//2  # リストの中央のインデックスを求める（整数除算 // で小数点以下切り捨て）
        if list == []:  # 【ベースケース】リストが空 → 探索失敗、該当なし
            return None
        elif list[m].id == query:  # 中央の要素のIDがクエリと一致 → 発見！
            return list[m].name  # 一致したバス停の名前を返す
        elif int(list[m].id[5:]) < int(query[5:]):  # 中央のID数値部分 < クエリのID数値部分
            # id[5:] → "ED01_1653" の6文字目以降 "1653" を取り出す
            # int() で整数に変換して数値として大小比較する
            # クエリの方が大きい → 目的のデータは右半分にある
            return self.search_impl(list[m+1:], query)  # 中央より右側（m+1～末尾）を再帰探索
        else:
            # クエリの方が小さい → 目的のデータは左半分にある
            return self.search_impl(list[0:m], query)  # 中央より左側（先頭～m-1）を再帰探索



# ============================================================
# メインの実行部分
# ============================================================
# ファイルからバス停データを読み込み、3つの探索アルゴリズムで
# 同じクエリを検索して実行時間を比較する。
# ============================================================

# --- データの読み込み ---
# allkyotobus_stop.dat ファイルを読み込みモードで開く
# encoding='utf-8' で日本語の文字化けを防ぐ
fi = open('allkyotobus_stop.dat', 'r', encoding='utf-8')

# readlines() でファイル全体を行ごとのリストとして読み込む
# 各要素は "ID 名前 緯度 経度 路線1 路線2 ..." という形式の文字列
lines = fi.readlines()

# 探索対象のIDリスト（3つのクエリで探索時間を比較する）
# ED01_1653 → データの前半にあるID（線形探索でも比較的早く見つかる）
# ED01_4089 → データの後半にあるID（線形探索では時間がかかる）
# ED01_5000 → 存在しないID（全アルゴリズムで「見つからない」ケースの比較）
queries = ['ED01_1653', 'ED01_4089', 'ED01_5000']

# --- 各クラスのインスタンス生成 ---
# 3つの探索アルゴリズムそれぞれに対応するDBインスタンスを作成
db1 = DBwLinerSearch()  # 線形探索用のインスタンス
db2 = DBwRecursiveSearch()  # 再帰的線形探索用のインスタンス
db3 = DBwBinarySearch()  # 二分探索用のインスタンス

# --- データの格納 ---
# ファイルから読み込んだ各行をパースし、Stopオブジェクトを作成して全DBに追加
for line in lines:
    line = line.rstrip()  # 行末の改行文字(\n)を除去
    items = line.split(' ')  # 1行を半角スペースで区切ってitemsリストに代入
    # items[0] → ID, items[1] → 名前, items[2] → 緯度, items[3] → 経度
    # items[4:] → 5番目以降の全要素（路線名のリスト）
    # float() で文字列を小数に変換（緯度・経度は数値として扱うため）
    stop = Stop(
        items[0],  # ID（例: "ED01_1653"）
        items[1],  # 名前（例: "立命館大学前"）
        float(items[2]),  # 緯度（文字列→小数に変換）
        float(items[3]),  # 経度（文字列→小数に変換）
        items[4:]  # 路線名リスト（5番目以降の全要素）
    )

    # 同じStopオブジェクトを3つのDBすべてに追加
    # ※ 各DBは独立したリストを持つが、格納されるデータは同じ
    db1.add(stop)  # 線形探索用DBに追加
    db2.add(stop)  # 再帰的線形探索用DBに追加
    db3.add(stop)  # 二分探索用DBに追加

# --- 実行時間の比較 ---
# 3つのDBインスタンスそれぞれに対して、3つのクエリで探索を実行し、
# 探索結果と実行時間を表示する
for db in [db1, db2, db3]:  # 3つの探索アルゴリズムを順番に実行
    for query in queries:  # 各クエリについて探索を実行
        start = time.perf_counter()  # 探索開始前の時刻を記録（perf_counterは高精度な時間計測用）
        result = db.search(query)  # 探索を実行（各クラスのsearch()が呼ばれる）
        elapsed_time = time.perf_counter() - start  # 探索にかかった時間を計算（秒単位）
        print(
            "ID:{}は{}です。{}は{}秒かかりました。".format(
                query,  # 検索したID
                result,  # 探索結果（バス停名 or None）
                db.__class__.__name__,  # クラス名（例: "DBwBinarySearch"）
                # __class__ はインスタンスが属するクラスオブジェクトを返す
                # __name__ はそのクラスの名前を文字列で返す
                elapsed_time  # 探索にかかった時間（秒）
            )
        )
fi.close()  # ファイルを閉じる（リソースの解放）
