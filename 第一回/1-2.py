fi = open('kyotocitybus_stop.dat', 'r', encoding='utf-8')
fo = open('kyotocitybus_stop_comma.dat', 'w', encoding='utf-8')

lines = fi.readlines()
for line in lines:
    line = line.rstrip()
    items = line.split(' ')
    fo.write(','.join(items) + '\n')

fi.close()
fo.close()

# -----------------------------------------------------------------
# 【以下、各行の詳細な解説】
#
# fi = open('kyotocitybus_stop.dat', 'r', encoding='utf-8')
# ［読み込み用のファイルを開く］
# 課題1と同じく、元のデータが入っているファイルを読み込みモード（'r'）で開きます。
#
# fo = open('kyotocitybus_stop_comma.dat', 'w', encoding='utf-8')
# ［書き込み用のファイルを開く（新規作成）］
# 変換したデータを保存するための新しいファイルを開きます。
# 'w' は「書き込みモード（Write）」の略です。この名前のファイルが無ければ新しく作り、
# もし既に同じ名前のファイルがあれば、中身を空にして上書きします。
# 開いたファイルの情報は変数 fo（file outputの略によく使われます）に代入します。
#
# lines = fi.readlines()
# ［中身をすべて読み込む］
# 元ファイル(fi)の中身をすべて読み込み、行ごとのリスト(配列)にまとめます。
#
# for line in lines:
# ［1行ずつ取り出して繰り返す］
# まとめたデータから1行ずつ取り出し、以下の処理を繰り返します。
#
#     line = line.rstrip()
#     ［見えないゴミ（改行）を消す］
#     行の末尾についている改行コードなどを削除して綺麗にします。
#
#     items = line.split(' ')
#     ［半角スペースで切り分ける］
#     1行の文字列を半角スペースの場所で分割し、バラバラのリストにします。
#
#     fo.write(','.join(items) + '\n')
#     ［カンマでくっつけて、ファイルに書き込む］
#     ここが課題2の最重要ポイントです。
#     ','.join(items) でカンマ区切りの文字列を作るところまでは課題1と同じです。
#     print() は自動で最後に改行してくれますが、ファイルに書き込む .write() は
#     自動で改行してくれません。そのため、文字列の最後に自分で改行コード（'\n'）を
#     足してから、書き込み用ファイル(fo)に書き込んでいます。
#
# fi.close()
# fo.close()
# ［ファイルを閉じる］
# 読み込み用(fi)と書き込み用(fo)、開いた2つのファイルを両方とも閉じます。
# 特に書き込み用のファイルは、ちゃんと .close() をしないと
# データが最後まで保存されないことがあるため、非常に重要です。
