fi = open('kyotocitybus_stop.dat', 'r', encoding='utf-8')

for line in fi:
    line = line.rstrip()
    items = line.split(' ') 
    
    if items[1] == '立命館大学前':
        routes = items[4:]
        print(','.join(routes))

fi.close()

# -----------------------------------------------------------------
# 【以下、各行の詳細な解説】
#
# fi = open('kyotocitybus_stop.dat', 'r', encoding='utf-8')
# ［ファイルを開く］
# これまでと同じく、データが入っているファイルを読み込みモード（'r'）で開きます。
#
# for line in fi:
# ［1行ずつ取り出して繰り返す］
# 今回は lines = fi.readlines() を使わず、開いたファイル(fi)から直接 for 文で
# 1行ずつ読み込んでいます。この書き方でも同じように1行ずつ順番に処理できます。
#
#     line = line.rstrip()
#     ［見えないゴミ（改行）を消す］
#     行の末尾についている改行コードなどを削除して綺麗にします。
#
#     items = line.split(' ')
#     ［半角スペースで切り分ける］
#     1行の文字列を半角スペースの場所で分割し、バラバラのリストにします。
#
#     if items[1] == '立命館大学前':
#     ［条件に合うかチェックする］
#     if は「もし〜なら」という条件分岐です。
#     プログラミングでは、リストの番号（インデックス）は「0」から始まります。
#     つまり、items[0] は1列目（バス停のIDなど）、items[1] は2列目（バス停の名前）です。
#     「もし2列目のデータが '立命館大学前' と完全に一致したら」以下の処理を行います。
#
#         routes = items[4:]
#         ［必要な部分だけを切り出す（スライス）］
#         課題1の画像にあった通り「5列目以降がバスの路線」のデータです。
#         5列目はインデックスでいうと「4」になります。
#         [4:] という書き方（スライスと呼びます）をすると、
#         「リストの4番目（5列目）から、一番最後まで」をごっそり切り出すことができます。
#         切り出した路線のリストを変数 routes に代入します。
#
#         print(','.join(routes))
#         ［カンマでくっつけて画面に出す］
#         切り出した路線のリスト(routes)を、カンマ(,)で繋いで1つの文字列にし、
#         画面に出力（表示）します。
#
# fi.close()
# ［ファイルを閉じる］
# 処理が終わったら忘れずにファイルを閉じます。
# -----------------------------------------------------------------