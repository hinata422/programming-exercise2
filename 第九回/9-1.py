# ============================================================
# 第9回 課題1：グラフの基本構造（隣接リスト表現）
# ============================================================
# このプログラムでは、京都市バスの路線ネットワークを「グラフ」として表現する。
# バス停をノード、バス停間の路線をエッジとして扱う。
#
# ■ プログラムの全体構成：
#   1. Neighbor クラス: 隣接ノード1つ分の情報（ID + エッジラベル）
#   2. Graph クラス: グラフ全体を管理するクラス（隣接リスト方式）
#   3. メイン部分: kyotocitybus_line.dat を読み込み、グラフを構築して表示
#
#
# ================================================================
# ■ グラフとは？（基本概念）
# ================================================================
#
# グラフ = 「ノード（頂点）」と「エッジ（辺）」で構成されるデータ構造
#
# 【イメージ】
#   バス停A ---[12号系統]--- バス停B ---[15号系統]--- バス停C
#
#   ノード: バス停A, バス停B, バス停C（各バス停が1つのノード）
#   エッジ: A-B間の12号系統、B-C間の15号系統（路線がエッジ）
#
# 【有向グラフと無向グラフの違い】
#   有向グラフ: エッジに方向がある（A→B と B→A は別のエッジ）
#     例: 一方通行の道路
#   無向グラフ: エッジに方向がない（A-B は双方向）
#     例: バス路線（行きも帰りも同じ路線を使える）
#     → このプログラムではバス路線を無向グラフとして扱う
#
#
# ================================================================
# ■ 隣接リスト表現とは？
# ================================================================
#
# グラフをプログラムで表現する方法の1つ。
# 各ノードごとに「隣接するノードのリスト」を持つ。
#
# 【具体例: バス路線ネットワーク】
#
#   データファイルの内容:
#     A B 京都市 12号系統
#     B C 京都市 15号系統
#
#   隣接リスト（node_list辞書）:
#     {
#       'A': [Neighbor('B', '12号系統')],
#       'B': [Neighbor('C', '15号系統'), Neighbor('A', '12号系統')],
#       'C': [Neighbor('B', '15号系統')]
#     }
#
#   ノードBは、AともCとも隣接している → 近傍リストに2つの要素がある
#
# 【隣接行列との違い】
#   隣接行列: n×n の2次元配列で全ペアの接続を記録 → メモリ O(n^2)
#   隣接リスト: 実際のエッジだけを記録 → メモリ O(n + e)（eはエッジ数）
#   → エッジが少ない疎グラフでは隣接リストの方がメモリ効率が良い
#
#
# ================================================================
# ■ insert(0, ...) の意味（エッジ追加の順序）
# ================================================================
#
# add_directed_edge では insert(0, Neighbor(...)) を使用している。
# insert(0, ...) はリストの先頭に要素を追加する操作。
#
# 【なぜ append ではなく insert(0, ...) なのか】
#   append: リストの末尾に追加 → 最初に追加したものが先頭
#   insert(0, ...): リストの先頭に追加 → 最後に追加したものが先頭
#
# 【影響】
#   データファイルを上から順に読み込むと、後に読み込んだエッジが
#   近傍リストの先頭に来る。第10回の walk メソッドでは neighbors[0]
#   （先頭の隣接ノード）を選ぶため、この順序が結果に影響する。
#
#   例: ノードAに対して 12号系統、15号系統、快速202号系統 の順にエッジ追加
#     insert後: [快速202号系統, 15号系統, 12号系統]
#     → neighbors[0] は 快速202号系統（最後に追加されたもの）
# ============================================================


# ============================================================
# Neighbor クラス — 隣接ノード1つ分の情報を保持
# ============================================================
# グラフの各ノードが持つ「隣接ノードリスト」の1要素。
# あるノードから見て、エッジでつながっている隣のノードの情報を表す。
#
# 【具体例】
#   ノードA が 12号系統 でノードB と接続している場合:
#     Neighbor('B', '12号系統') がノードAの近傍リストに入る
#
#   属性:
#     id = 'B'          ← 隣接ノードのID
#     label = '12号系統' ← 接続するエッジのラベル（路線名）
# ============================================================
class Neighbor:
    def __init__(self, id, label):
        self.id = id  # 隣接ノードのID
        self.label = label  # 隣接ノードと接続するエッジのラベル


# ============================================================
# Graph クラス — グラフ全体を管理するクラス
# ============================================================
# 隣接リスト方式でグラフを表現する。
#
# 【内部データ構造】
#   self.node_list: 辞書型（dict）
#     key   = ノードID（文字列、例: 'ED01_1914'）
#     value = Neighborオブジェクトのリスト（そのノードの近傍リスト）
#
#   例:
#     {
#       'ED01_1914': [Neighbor('ED01_1936', '快速202号系統'), Neighbor('ED01_1915', '12号系統')],
#       'ED01_1936': [Neighbor('ED01_1914', '快速202号系統')],
#       'ED01_1915': [Neighbor('ED01_1914', '12号系統')]
#     }
#
# 【主なメソッド】
#   add_node(id)                     : ノードを追加
#   add_directed_edge(sid, tid, label): 有向エッジを追加（sid → tid）
#   add_undirected_edge(sid, tid, label): 無向エッジを追加（sid ↔ tid）
#   get_neighborhood(id)             : 指定ノードの近傍リストを取得
#   print_graph()                    : 全エッジを表示
# ============================================================
class Graph:
    def __init__(self):
        self.node_list = {}  # グラフ上のノードリスト(辞書：key=ノードID, value=近傍リスト)
        self.num_nodes = 0  # グラフ上のノードの数
        self.num_edges = 0  # グラフ上のエッジの数

    # ----------------------------------------------------------
    # add_node: グラフにノードを1つ追加するメソッド
    # ----------------------------------------------------------
    # 引数 id: 追加するノードのID（文字列）
    #
    # 既にノードが存在する場合は何もしない（重複追加を防ぐ）。
    # 新しいノードの近傍リストは空リスト [] で初期化する。
    # ----------------------------------------------------------
    def add_node(self, id):  # グラフにノードを追加するメソッド
        if id not in self.node_list:  # ノードリストにidのノードが無ければ
            self.node_list[id] = []  # ノードリストにidのノードを追加する．ただし、近傍はないので[]を代入
            self.num_nodes += 1  # ノードの数をインクリメント

    # ----------------------------------------------------------
    # add_directed_edge: グラフに有向エッジを追加するメソッド
    # ----------------------------------------------------------
    # 引数 sid: 始点ノードのID（source）
    # 引数 tid: 終点ノードのID（target）
    # 引数 label: エッジのラベル（路線名など）
    #
    # sid → tid の方向にのみエッジを追加する。
    # 始点・終点のノードが存在しない場合は自動的に追加する。
    #
    # 【insert(0, ...) の動作】
    #   近傍リストの先頭に追加する（後から追加したものが先頭に来る）
    #   例: node_list['A'] = [Neighbor('C', '15号')]
    #       insert(0, Neighbor('B', '12号')) 実行後
    #       node_list['A'] = [Neighbor('B', '12号'), Neighbor('C', '15号')]
    # ----------------------------------------------------------
    def add_directed_edge(self, sid, tid, label):  # グラフに有向エッジを追加するメソッド
        if sid not in self.node_list:  # 始点のノードが無ければ追加
            self.add_node(sid)
        if tid not in self.node_list:  # 終点のノードが無ければ追加
            self.add_node(tid)
        self.node_list[sid].insert(0, Neighbor(tid, label))  # 近傍リストの先頭に終点ノードを追加
        self.num_edges += 1  # エッジの数をインクリメント

    # ----------------------------------------------------------
    # add_undirected_edge: グラフに無向エッジを追加するメソッド
    # ----------------------------------------------------------
    # 引数 sid, tid, label: 有向エッジと同じ
    #
    # 無向エッジ = 双方向のエッジ。
    # sid → tid と tid → sid の2つの有向エッジを追加することで実現する。
    #
    # 【エッジ数の調整】
    #   add_directed_edge を2回呼ぶと num_edges が2回インクリメントされるが、
    #   無向エッジは1本なので、1回デクリメントして帳尻を合わせる。
    #   例: A-B間の無向エッジ1本 → 内部的にはA→B, B→Aの2方向だが、
    #       エッジとしては1本とカウントする。
    # ----------------------------------------------------------
    def add_undirected_edge(self, sid, tid, label):  # グラフに無向エッジを追加するメソッド
        self.add_directed_edge(sid, tid, label)  # sid → tid のエッジを追加
        self.add_directed_edge(tid, sid, label)  # tid → sid のエッジを追加（逆方向）
        self.num_edges -= 1  # エッジの数が重複するのでデクリメント（2回加算されたので1回引く）

    # ----------------------------------------------------------
    # get_node_list: 全ノードの辞書を返すメソッド
    # ----------------------------------------------------------
    def get_node_list(self):
        return self.node_list

    # ----------------------------------------------------------
    # get_neighborhood: 指定ノードの近傍リスト（Neighborのリスト）を返す
    # ----------------------------------------------------------
    # 引数 id: 近傍を知りたいノードのID
    # 返り値: Neighborオブジェクトのリスト、または None（ノードが存在しない場合）
    # ----------------------------------------------------------
    def get_neighborhood(self, id):
        if id in self.node_list:
            return self.node_list[id]
        else:
            return None

    # ----------------------------------------------------------
    # get_num_nodes / get_num_edges: ノード数・エッジ数を返すメソッド
    # ----------------------------------------------------------
    def get_num_nodes(self):
        return self.num_nodes

    def get_num_edges(self):
        return self.num_edges

    # ----------------------------------------------------------
    # print_graph: 隣接リストを全て表示するメソッド
    # ----------------------------------------------------------
    # 全ノードについて、そのノードに隣接する全ノードとエッジラベルを表示する。
    # 例: ED01_1914 is connected to ED01_1936 by 快速202号系統.
    # ----------------------------------------------------------
    def print_graph(self):  # 隣接リストを表示するメソッド
        for id in self.get_node_list():  # 全ノードを順に取得
            for neighbor in self.get_neighborhood(id):  # 各ノードの近傍を順に取得
                print('{} is connected to {} by {}.'.format(id, neighbor.id, neighbor.label))


# ============================================================
# メインの実行部分
# ============================================================
if __name__ == '__main__':  # モジュールとしてインポートされるときは実行しない

    # --- データの読み込み ---
    # kyotocitybus_line.dat のフォーマット:
    #   バス停ID1 バス停ID2 京都市 路線名
    # 例: ED01_1914 ED01_1915 京都市 12号系統
    fi = open('kyotocitybus_line.dat', 'r', encoding = 'utf-8')
    bus_network = Graph()  # 空のグラフを作成
    lines = fi.readlines()  # ファイル全体を行ごとのリストとして読み込む

    # --- グラフの構築 ---
    for line in lines:
        line = line.rstrip()  # 行末の改行文字を除去
        items = line.split(' ')  # 1行を半角スペースで区切ってitemsリストに代入
        # items[0] = バス停ID1, items[1] = バス停ID2, items[2] = '京都市', items[3] = 路線名
        bus_network.add_undirected_edge(items[0], items[1], items[3])  # 無向エッジを追加

    # --- 結果の表示 ---
    print('ノード(バス停)の数は{}個'.format(bus_network.get_num_nodes()))
    print('エッジ(バス停間のリンク)の数は{}個'.format(bus_network.get_num_edges()))
    bus_network.print_graph()  # 全エッジを表示
    fi.close()  # ファイルを閉じる
