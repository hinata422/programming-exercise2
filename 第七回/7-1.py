# ============================================================
# 第7回 課題1：ヒープにデータを挿入する
# ============================================================
# このプログラムでは、ヒープ（Heap）と呼ばれるデータ構造を実装し、
# バス停データを経度(lng)の昇順で管理する。
#
# ■ ヒープとは：
#   ヒープとは「ヒープ条件」を満たす「完全二分木」のことである。
#
#   【完全二分木とは】
#     - 各ノード（節）が最大2つの子を持つ木構造
#     - 全てのレベルが左詰めで埋まっている
#     - 例（数値は経度とする）：
#
#              1
#            /   \
#           3     2
#          / \   /
#         5   4 6
#
#   【ヒープ条件（最小ヒープ）とは】
#     - ある節の値が、その子孫のどの節よりも小さいこと
#     - つまり、どの節も自分の親よりも値が大きい
#     - 結果として、根（ルート）が常に最小値になる
#
# ■ リストによる完全二分木の実装：
#   完全二分木はリスト（配列）で効率的に表現できる。
#   インデックス0はダミー（使わない）とし、インデックス1から使う。
#
#   リスト: [ダミー, 根, 左子, 右子, 左左孫, 左右孫, 右左孫, ...]
#   添字:   [  0,    1,   2,   3,    4,     5,     6,    ...]
#
#   あるノードのインデックスを i とすると：
#     - 親のインデックス:    i // 2
#     - 左の子のインデックス: i * 2
#     - 右の子のインデックス: i * 2 + 1
#
#   例: インデックス3のノードの親は 3//2=1（根）、
#       左の子は 3*2=6、右の子は 3*2+1=7
#
# ■ insert（挿入）のアルゴリズム：
#   1. 新しいデータをリストの末尾に追加する
#   2. shift_up で、追加した要素をヒープ条件を満たす位置まで押し上げる
#
#   【shift_up の動作イメージ】
#     例: ヒープ [_, 1, 3, 2, 5, 4] に 0 を挿入する場合
#
#     ステップ1: 末尾に追加 → [_, 1, 3, 2, 5, 4, 0]
#                    1
#                  /   \
#                 3     2       ← 0 は 2 の右の子（インデックス6）
#                / \   / \
#               5   4 0
#
#     ステップ2: shift_up(6) — 親(インデックス3)の 2 と比較 → 0 < 2 → 交換
#               [_, 1, 3, 0, 5, 4, 2]
#                    1
#                  /   \
#                 3     0
#                / \   / \
#               5   4 2
#
#     ステップ3: shift_up(3) — 親(インデックス1)の 1 と比較 → 0 < 1 → 交換
#               [_, 0, 3, 1, 5, 4, 2]
#                    0
#                  /   \
#                 3     1
#                / \   / \
#               5   4 2
#
#     ステップ4: shift_up(1) — インデックス1は根なので終了
#     → ヒープ条件を満たした！
# ============================================================

# 穴埋め問題用のプログラム
from def_stop import Stop  # Stopクラスの読み込み（バス停1件分のデータを表すクラス）
import sys  # 再帰呼び出し回数の上限を変更するためのモジュール
# Pythonはデフォルトで再帰の深さが約1000回に制限されている。
# バス停データが数千件あるため、再帰が深くなる可能性があるので上限を引き上げる。
sys.setrecursionlimit(10000)  # 再帰呼び出し回数の上限を10,000回に変更


class Heap:  # Heapクラスの定義
    def __init__(self):
        # self.list: ヒープのデータを格納するリスト
        # インデックス0にはダミー値(0)を入れ、実際のデータはインデックス1から格納する。
        # こうすることで、親子のインデックス計算が簡単になる：
        #   親 = i // 2,  左子 = i * 2,  右子 = i * 2 + 1
        self.list = [0]  # データを格納するリスト。0番目の要素は使わないため0を代入。

        # self.size: 現在ヒープに格納されているデータの件数
        # （self.list の長さ - 1 と同じ値になる。ダミーの分を除くため）
        self.size = 0

    def insert(self, data):
        # データをヒープに挿入するメソッド
        # 引数 data: 挿入する Stop インスタンス

        # ステップ1: データをリストの末尾に追加する
        # この時点ではヒープ条件を満たしていない可能性がある
        self.list.append(data)  # データをヒープの最後の要素の次に挿入

        # ステップ2: サイズを1増やす
        self.size += 1  # ヒープサイズを1増やす

        # ステップ3: 挿入した要素（末尾 = self.size の位置）をシフトアップする
        # シフトアップにより、ヒープ条件を満たす正しい位置まで押し上げられる
        # 穴埋め: self.size を渡す（末尾に追加した要素のインデックス）
        self.shift_up(self.size)  # 挿入した要素をシフトアップする

    def shift_up(self, num):  # num番目の要素をシフトアップ
        # num番目の要素を、ヒープ条件を満たす位置まで上に移動させるメソッド（再帰）
        # 引数 num: シフトアップしたい要素のインデックス
        #
        # 処理の流れ：
        #   1. num が根（インデックス1）なら何もしない → 終了条件
        #   2. 親（num // 2）の経度が自分より大きければ → 親子を交換
        #   3. 交換後、親の位置（num // 2）から再帰的にシフトアップを続ける
        #
        # 穴埋め箇所：
        #   - num > 1: インデックス1が根なので、1より大きい間は親が存在する
        #   - num // 2: 親のインデックス（整数除算）

        # num番目の要素が根でなく（num > 1）、
        # かつ親（num // 2）の経度が自分の経度より大きい場合、親と入れ替え
        if num > 1 and self.list[num // 2].lng > self.list[num].lng:
            # Pythonの多重代入で親子の値を交換（スワップ）
            self.list[num], self.list[num // 2] = self.list[num // 2], self.list[num]

            # 交換後、元の親の位置（num // 2）に移動した要素について、
            # さらに上の親と比較するため再帰呼び出し
            # 穴埋め: num // 2（交換先 = 親の位置から再帰）
            self.shift_up(num // 2)  # 入れ替え後に、引き続き新しい親のシフトアップ

    def show_tree(self, num):  # num番目の要素を根とする木（もしくは部分木）を表示するメソッド
        # ヒープを横向きの木構造として表示する（再帰）
        # 引数 num: 表示する部分木の根のインデックス
        #
        # 表示順序: 右の子 → 自分 → 左の子（中間順巡回の逆版）
        # これにより、画面上で右の子が上、左の子が下に表示され、
        # 90度回転した木のように見える。
        #
        # 出力例：
        #       右子:値
        #     親:値
        #       左子:値
        #   根:値
        #       右子:値
        #     親:値
        #       左子:値

        if num <= self.size:  # 要素numがヒープサイズの範囲内である限り

            # ステップ1: まず右の子（num * 2 + 1）の部分木を再帰的に表示
            # 穴埋め: num * 2 + 1（右の子のインデックス）
            self.show_tree(num * 2 + 1)  # 要素numの右の子を根とする部分木を表示

            # ステップ2: 自分自身を表示（深さに応じたインデント付き）
            i = num
            space = ''
            # ノードの深さを計算し、深さ分だけスペースを追加
            # i // 2 を繰り返すと根（1）に到達するまでの回数 = そのノードの深さ
            # 例: num=6 → 6//2=3 → 3//2=1 → 1//2=0(ループ終了) → 深さ2 → スペース4個
            while i // 2 > 0:  # 要素numの深さ分だけ半角スペース2個を連結
                space += '  '  # 1段につき半角スペース2個
                i = i // 2

            # 「インデント + バス停名:経度」の形式で表示
            print('{}{}:{}'.format(space, self.list[num].name, self.list[num].lng))

            # ステップ3: 左の子（num * 2）の部分木を再帰的に表示
            # 穴埋め: num * 2（左の子のインデックス）
            self.show_tree(num * 2)  # 要素numの左の子を根とする部分木を表示

    def is_heap(self):  # ヒープ条件のチェック
        # 全てのノードについて、自分の経度が親の経度以上であるかを検証する
        # range(self.size, 1, -1) → 末尾からインデックス2まで逆順にループ
        # i//2 が親のインデックス
        for i in range(self.size, 1, -1):
            # 子の経度が親の経度より小さい → ヒープ条件違反
            if self.list[i].lng < self.list[i//2].lng:
                return False
        return True  # 全ノードがヒープ条件を満たしていればTrue


# ============================================================
# メインの実行部分
# ============================================================
if __name__ == '__main__':  # モジュールとしてインポートされるときは実行しない

    # --- データの読み込み ---
    fi = open('allkyotobus_stop.dat', 'r', encoding='utf-8')
    lines = fi.readlines()  # ファイル全体を行ごとのリストとして取得

    # Heapインスタンスを作成
    heap = Heap()

    # --- データの挿入 ---
    for line in lines:
        line = line.rstrip()  # 行末の改行文字を除去
        items = line.split(' ')  # 1行を半角スペースで区切ってitemsリストに代入
        # items[0]=ID, items[1]=名前, items[2]=緯度, items[3]=経度, items[4:]=路線リスト
        # Stop インスタンスを生成し、insert メソッドでヒープに挿入
        # insert するたびに shift_up が呼ばれ、ヒープ条件が維持される
        heap.insert(Stop(items[0], items[1], float(items[2]), float(items[3]), items[4:]))

    # --- 結果の表示 ---
    # is_heap() でヒープ条件を満たしているか検証
    if heap.is_heap():
        # 穴埋め: 1（根のインデックス）からヒープ全体を表示
        heap.show_tree(1)  # ヒープ全体を表示

    fi.close()  # ファイルを閉じる
