# ============================================================
# 第11回 課題1：幅優先探索（BFS）で全てのノードを辿る
# ============================================================
# このプログラムでは、第10回で使った Graph クラスを継承し、
# 幅優先探索（BFS: Breadth-First Search）で全ノードを訪問する
# search_with_BFS メソッドを備えた GraphSearch クラスを定義する。
#
# ■ プログラムの全体構成：
#   1. def_graph.py から Graph クラスを読み込む
#   2. def_queue_stack.py から Queue クラスを読み込む（BFS にはキューが必要）
#   3. def_stop.py から Stop クラスを読み込む（バス停名の表示用）
#   4. GraphSearch クラス: Graph を継承し、BFS と最長パス表示を追加
#   5. メイン部分: グラフとバス停データを読み込み、BFS を実行
#
#
# ================================================================
# ■ 第10回のウォーク（walk）との違い
# ================================================================
#
# 【第10回のウォーク】
#   - 1本の道を辿るだけ（1つの隣接ノードしか選ばない）
#   - step 回だけ移動して終了
#   - 全ノードを訪問することはできない
#
# 【第11回の幅優先探索（BFS）】
#   - あるノードの全ての未訪問隣接ノードを発見してからキューに入れ、
#     近いノードから順に網羅的に訪問する
#   - step の制限なし → 到達可能な全ノードを訪問する
#   - キュー（FIFO）を使うことで「近い順」の訪問を保証する
#
#
# ================================================================
# ■ 幅優先探索（BFS）とは？
# ================================================================
#
# BFS は、グラフの各ノードを「スタート地点に近い順」に訪問する探索手法。
#
# 【イメージ: 池に石を投げたときの波紋】
#   スタート地点を中心に、同心円状に外側へ広がっていく。
#   1ホップで行ける全ノード → 2ホップで行ける全ノード → 3ホップ → ...
#   という順番で訪問する。
#
#     ホップ0:  [S]            ← スタート
#     ホップ1:  [A, B, C]      ← Sの隣接ノード全部
#     ホップ2:  [D, E, F, G]   ← A,B,Cの隣接ノードのうち未訪問のもの
#     ホップ3:  [H, I]         ← D,E,F,Gの隣接ノードのうち未訪問のもの
#     ...
#
# 【なぜキュー（FIFO）を使うのか？】
#   キューは「先に入れたものが先に出る」データ構造。
#   ホップ1のノードを全部キューに入れてから、ホップ2のノードを入れる。
#   取り出す時はホップ1のノードが先に出てくるので、
#   「近いノードから順に処理する」ことが保証される。
#
#   例:
#     enqueue(A), enqueue(B), enqueue(C)  ← ホップ1を全部入れる
#     dequeue() → A  ← 先に入れたAが先に出る
#     Aの隣接: enqueue(D), enqueue(E)    ← ホップ2が後ろに追加される
#     dequeue() → B  ← まだホップ1のBが残っている
#     dequeue() → C  ← ホップ1のC
#     dequeue() → D  ← ここからホップ2
#     dequeue() → E
#
#
# ================================================================
# ■ BFS のアルゴリズム
# ================================================================
#
# 【使用するデータ構造】
#
#   queue: Queue オブジェクト — 次に処理するノードIDを格納
#     FIFO（先入れ先出し）で取り出すことで、近い順の訪問を保証
#
#   visited: リスト — 訪問済みノードIDのリスト
#     同じノードを重複してキューに入れないために使う
#     ※ キューに入れた時点で visited に追加する（取り出した時点ではない）
#
#   parents: 辞書 — 各ノードへの到達経路を記録
#     key = 訪問先ノードID
#     value = (直前のノードID, エッジラベル) のタプル、または None（開始地点）
#     例: {'ED01_1914': None, 'ED01_1936': ('ED01_1914', '快速202号系統')}
#
# 【アルゴリズムの流れ】
#
#   1. キューを作成し、start を enqueue
#   2. visited に start を追加、parents[start] = None
#   3. キューが空でない間:
#      a. キューから1つ dequeue → current（現在位置）
#      b. current の全隣接ノードを走査
#      c. 未訪問の隣接ノードがあれば:
#         - visited に追加
#         - parents に経路情報を記録
#         - キューに enqueue
#   4. 全ノード訪問後、trace_longest_path で最長パスを表示
#
# 【具体例: 小さなグラフでの動作】
#
#   グラフ:  S --[a]--> A --[b]--> D
#            |          |
#           [c]        [d]
#            |          |
#            B --[e]--> C
#
#   ステップ1: dequeue → S
#     隣接: [A, B]（未訪問）
#     visited = [S, A, B]
#     queue = [A, B]
#
#   ステップ2: dequeue → A  ← Sに近いノードから処理
#     隣接: [D, C]（未訪問）
#     visited = [S, A, B, D, C]
#     queue = [B, D, C]
#
#   ステップ3: dequeue → B  ← まだホップ1のBが残っている
#     隣接: [C]（既に訪問済み）→ スキップ
#     queue = [D, C]
#
#   ステップ4: dequeue → D  ← ホップ2
#     隣接: なし
#     queue = [C]
#
#   ステップ5: dequeue → C  ← ホップ2
#     隣接: [B]（既に訪問済み）→ スキップ
#     queue = []  ← 空になったので終了
#
#   parents = {S: None, A: (S, a), B: (S, c), D: (A, b), C: (A, d)}
#   → 各ノードへの最短経路が記録されている！
#
#
# ================================================================
# ■ trace_longest_path のアルゴリズム
# ================================================================
#
# BFS で構築した parents 辞書を使って、スタート地点から最も遠い
# （ホップ数が最大の）ノードまでのパスを表示するメソッド。
#
# 【アルゴリズムの流れ】
#   1. parents 辞書の全ノードについて:
#      a. そのノードから parents を逆順に辿ってスタート地点まで遡る
#      b. 辿ったホップ数をカウント
#      c. これまでの最大ホップ数を超えていれば更新
#   2. 最大ホップ数のパスを表示
#
# 【逆順に辿る仕組み】
#   parents[dest] = (前のノード, エッジラベル)
#   parents[前のノード] = (さらに前のノード, エッジラベル)
#   ...
#   parents[start] = None  ← ここで停止
#
#   例: dest = D の場合
#     parents[D] = (A, 'b') → current = A
#     parents[A] = (S, 'a') → current = S
#     parents[S] = None → ループ終了
#     hops = 2, path = "S --[a]--> A --[b]--> D"
#
#
# ================================================================
# ■ 穴埋め問題の解説
# ================================================================
#
#   空欄1: queue = ____  → Queue()
#     理由: BFS ではキュー（FIFO）を使って訪問順を管理する。
#           Queue クラスのインスタンスを作成する。
#
#   空欄2: queue.____(start)  → enqueue
#     理由: Queue の追加操作は enqueue。
#           開始ノードをキューに入れて探索を開始する。
#
#   空欄3: current = queue.____()  → dequeue
#     理由: Queue の取り出し操作は dequeue。
#           先に入れたノード（＝近いノード）から順に取り出す。
#
#   空欄4: self.____(current)  → get_neighborhood
#     理由: Graph クラスの get_neighborhood メソッドで
#           現在位置の隣接ノードリスト（Neighbor オブジェクト）を取得する。
#
#   空欄5: not in ____  → visited
#     理由: 訪問済みリスト visited に含まれていない
#           （＝未訪問の）ノードだけを探索対象にする。
#
#   空欄6: queue.____(neighbor.id)  → enqueue
#     理由: 未訪問の隣接ノードを発見したらキューに追加し、
#           後で処理されるようにする。
#
#
# ================================================================
# ■ 実行結果
# ================================================================
#
#   府道横大路まで37ホップ：立命館大学前 --[快速202号系統]--> 小松原児童公園前
#   --[快速202号系統]--> 衣笠校前 --[快速202号系統]--> 北野白梅町 ...
#   → BFS で到達可能な全ノードの中で、最もホップ数が多いパスを表示
# ============================================================

# 穴埋め問題用のプログラム
from def_graph import Graph  # Graphクラスの読み込み
from def_queue_stack import Stack, Queue  # StackクラスおよびQueueクラスの読み込み
from def_stop import Stop  # Stopクラスの読み込み


# ============================================================
# GraphSearch クラス — Graph を継承して BFS 探索機能を追加
# ============================================================
# 【継承の構成】
#   Graph（def_graph.py）
#     ├─ node_list: ノード辞書（key=ノードID, value=隣接ノードリスト）
#     ├─ add_undirected_edge(): 無向エッジの追加
#     ├─ get_neighborhood(): 隣接ノードの取得
#     └─ ...
#   GraphSearch（このファイル）← Graph を継承
#     ├─ busstops: バス停情報辞書（key=バス停ID, value=Stopオブジェクト）
#     ├─ search_with_BFS(): 幅優先探索で全ノードを訪問
#     └─ trace_longest_path(): 最長パスを表示
# ============================================================
class GraphSearch(Graph):
    def __init__(self):
        super().__init__()  # Graphクラスから継承したインスタンス変数の初期化
        self.busstops = {}  # バス停情報を格納するための辞書データ（key: バス停ID、value: Stopオブジェクト）
        # busstops はバス停IDからバス停名（name）を取得するために使う
        # 例: self.busstops['ED01_1914'].name → '立命館大学前'

    # ----------------------------------------------------------
    # search_with_BFS: 幅優先探索で全ノードを訪問するメソッド
    # ----------------------------------------------------------
    # 引数 start: 探索開始ノードのID（文字列）
    #
    # 【第10回の walk との違い】
    #   walk: 1つの隣接ノードだけを選んで移動（1本道）
    #   BFS: 全ての未訪問隣接ノードをキューに入れて網羅的に訪問
    #
    # 【キューの役割】
    #   キューに入れた順番（＝発見した順番）で処理するため、
    #   スタート地点に近いノードから順に訪問される。
    # ----------------------------------------------------------
    def search_with_BFS(self, start):
        # --- 初期化 ---
        queue = Queue()  # 【穴埋め1】幅優先探索用のキューを作成
        # Queue()でFIFO（先入れ先出し）のキューオブジェクトを生成
        # enqueue で末尾に追加、dequeue で先頭から取り出す

        parents = {start: None}  # 訪問したノードの一つ前のノードとエッジを格納
        # key: 訪問ノードID, value: (一つ前のノードID, エッジラベル) のタプル
        # 開始地点の直前はないので None
        # 例: {'ED01_1914': None,
        #       'ED01_1936': ('ED01_1914', '快速202号系統'),
        #       'ED01_1927': ('ED01_1936', '快速202号系統'), ...}

        visited = [start]  # 訪問済みリストにstartを追加
        # ※ visited はキューに入れた時点で追加する（dequeue時ではない）
        # → 同じノードが重複してキューに入るのを防ぐため

        queue.enqueue(start)  # 【穴埋め2】キューに訪問済みのノードを追加
        # 開始ノードをキューに入れて探索を開始する

        # --- BFS のメインループ ---
        while not queue.is_empty():  # キューが空でない限り以下を繰り返す
            # キューが空 ＝ 到達可能な全ノードを訪問し終えた

            current = queue.dequeue()  # 【穴埋め3】訪問済みノードを取り出し現在位置に設定
            # dequeue: 先頭から取り出す（FIFO）
            # → 先にキューに入れた（＝スタートに近い）ノードから処理される

            if current not in self.node_list:  # 訪問中ノードが存在しなければ
                print('{}のノードがグラフ上に存在しません。'.format(current))
                return

            neighbors = self.get_neighborhood(current)  # 【穴埋め4】現在位置の隣接ノードを取得
            # get_neighborhood: Neighbor オブジェクト（.id と .label を持つ）のリストを返す

            for neighbor in neighbors:  # 隣接ノードがある限り以下を繰り返す
                if neighbor.id not in visited:  # 【穴埋め5】訪問先候補が未訪問であれば
                    # visited リストに含まれていない ＝ まだ発見されていないノード

                    parents[neighbor.id] = (current, neighbor.label)  # 現在位置を訪問先候補の一つ前のノードに設定
                    # parents に経路情報を記録 → 後で trace_longest_path でパスを復元するために使う

                    visited.append(neighbor.id)  # 訪問先候補を訪問済みリストに追加
                    # ※ ここで visited に追加するのが重要
                    # もし dequeue 時に追加すると、同じノードが複数回キューに入る可能性がある

                    queue.enqueue(neighbor.id)  # 【穴埋め6】訪問済みノードをキューに追加
                    # 未訪問の隣接ノードをキューの末尾に追加
                    # → 現在のホップ数のノードが全て処理された後に処理される

        self.trace_longest_path(parents)  # 最長パスを表示

    # ----------------------------------------------------------
    # trace_longest_path: BFS で記録した parents から最長パスを表示
    # ----------------------------------------------------------
    # 引数 parents: 辞書（key=ノードID, value=(直前ノードID, エッジラベル) or None）
    #
    # 【仕組み】
    #   parents 辞書の各ノードを目的地として、そこからスタート地点まで
    #   parents を逆順にたどる。たどったホップ数が最大のパスを表示する。
    #
    # 【なぜ BFS の parents で最長パスが分かるのか】
    #   BFS の parents は各ノードへの「最短経路」を記録している。
    #   その中でホップ数が最大のもの ＝ BFS ツリー上で最も遠いノード。
    # ----------------------------------------------------------
    def trace_longest_path(self, parents):  # 最長のパスを表示するメソッド
        max_hops = 0  # 最長パスのホップ数
        longest_path = ''  # 最長パスの経路（文字列）
        furthest_dest = ''  # 最長パスの目的地ノードID
        for dest in parents:  # 各ノードを目的地として以下の処理を繰り返す
            current = dest  # 目的地を現在位置とする
            path = self.busstops[current].name  # 現在位置のバス停名を取得
            # busstops 辞書からバス停IDに対応する Stop オブジェクトの name を取得
            hops = 0  # 目的地までの距離（ホップ数）を初期化
            parent = parents[current]  # 現在位置の一つ前のバス停と接続するエッジのラベルを取得
            while parent is not None:
            # 現在位置の一つ前のバス停がある限り（現在位置がスタート地点でない限り）以下を繰り返す
                current = parent[0]  # 現在位置を一つ前のバス停に移動
                # parent[0] = 直前のノードID
                path = self.busstops[current].name + ' --[' + parent[1] + ']--> ' + path
                # parent[1] = エッジラベル（路線名）
                # 一つ前のバス停とたどったエッジのラベル（路線）をpathの先頭に連結
                hops += 1  # ホップ数をインクリメント
                parent = parents[current]  # 現在位置の一つ前のバス停と接続するエッジのラベルを取得
            if hops > max_hops:  # 仮の最大ホップ数を超えれば
                max_hops = hops  # 仮の最大ホップ数を更新
                longest_path = path  # 仮の最長パスを更新
                furthest_dest = dest  # 仮の最長パスの目的地を更新
        print('{}まで{}ホップ：{}'.format(self.busstops[furthest_dest].name, max_hops, longest_path))


# ============================================================
# メインの実行部分
# ============================================================
if __name__ == '__main__':  # モジュールとしてインポートされるときは実行しない

    # --- 路線データの読み込みとグラフ構築 ---
    fi = open('kyotocitybus_line.dat', 'r', encoding='utf-8')
    bus_network = GraphSearch()  # GraphSearch のインスタンスを作成
    lines = fi.readlines()
    for line in lines:
        line = line.rstrip()
        items = line.split(' ')  # 1行を半角スペースで区切ってitemsリストに代入
        # items[0]=バス停ID1, items[1]=バス停ID2, items[2]='京都市', items[3]=路線名
        bus_network.add_undirected_edge(items[0], items[1], items[3])  # 無向エッジを追加

    # --- バス停データの読み込み ---
    # バス停IDとバス停名を関連付けるために busstops 辞書に Stop オブジェクトを格納
    fi2 = open('kyotocitybus_stop.dat', 'r', encoding='utf-8')
    lines = fi2.readlines()
    for line in lines:
        line = line.rstrip()
        items = line.split(' ')  # 1行を半角スペースで区切ってitemsリストに代入
        # items[0]=バス停ID, items[1]=バス停名, items[2]=緯度, items[3]=経度, items[4:]=路線リスト
        bus_network.busstops[items[0]] = Stop(items[0], items[1], items[2], items[3], items[4:])

    # --- BFS の実行 ---
    # ED01_1914（立命館大学前）から全ノードを幅優先探索し、最長パスを表示
    bus_network.search_with_BFS('ED01_1914')
    fi.close()
    fi2.close()
